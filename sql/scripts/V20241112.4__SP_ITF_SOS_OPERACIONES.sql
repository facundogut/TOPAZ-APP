concat(right(concat(Replicate(''0'',4),sos.Operacion),4), sos.Subsistema, RIGHT(concat(Replicate(''0'',7), sos.CodTransaccion),7)) AS CODIGOSTOPAZ,
--19
ISNULL((CASE WHEN mov.MONEDA = 1 THEN 0 ELSE 

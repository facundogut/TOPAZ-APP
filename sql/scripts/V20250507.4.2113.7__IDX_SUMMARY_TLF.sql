EXEC('
IF NOT EXISTS (
    SELECT 1 
    FROM sys.indexes 
    WHERE name = ''IDX_CONCILIACION_SUMMARY_TLF'' 
      AND object_id = OBJECT_ID(''dbo.TJD_TLF_SUMMARY'')
)
BEGIN
    CREATE NONCLUSTERED INDEX [IDX_CONCILIACION_SUMMARY_TLF]
    ON [dbo].[TJD_TLF_SUMMARY] (
        [CODIGOPROCESAMIENTO],
        [FECHATRANSACCION],
        [HORATRANSACCION],
        [NUMEROTRANSACCION],
        [IDENTIFICACIONCAJERO]
    )
    INCLUDE ([FROMACCOUNT],[TOACCOUNT],[CONCILIACION]);
END
');

EXEC('
IF NOT EXISTS (
    SELECT 1 
    FROM sys.indexes 
    WHERE name = ''IDX_CONCILIACION_TLF_SUMMARY'' 
      AND object_id = OBJECT_ID(''dbo.TLF_CONCILIACION_CABECERA'')
)
BEGIN
    CREATE NONCLUSTERED INDEX [IDX_CONCILIACION_TLF_SUMMARY]
    ON [dbo].[TLF_CONCILIACION_CABECERA] (
        [FECHA_PROCESO],
        [ESTADO_CONCILIACION]
    )
    INCLUDE ([LKRVSL],[COD_MSJ_TLF],[LKTRAD],[LKTRAT],[LKSEQN],[LKTERM],[COD_MSJ_H2H],[LKFROM],[LKTOAC]);
END
');

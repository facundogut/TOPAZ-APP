EXECUTE('
	IF OBJECT_ID (''dbo.VW_PARAM_TASA_FIJA_DPF'') IS NOT NULL
	DROP VIEW dbo.VW_PARAM_TASA_FIJA_DPF
')

EXECUTE('
	CREATE VIEW [dbo].[VW_PARAM_TASA_FIJA_DPF] AS
	SELECT 
		DPF.PRODUCTO AS Producto,  
		DPF.MONEDA AS Moneda,  
		DPF.SECTOR AS Sector,  
		DPF.PUNTOS_PORCENTUALES AS Puntos_Porcentuales,
		DPF.FECHA AS Fecha
	FROM
		DPF_PARAM_TASA_PRECANCELACION DPF
	WHERE ((DPF.TZ_LOCK < 100000000000000 OR DPF.TZ_LOCK >= 200000000000000)
		AND (DPF.TZ_LOCK < 300000000000000 OR DPF.TZ_LOCK >= 400000000000000))
')
EXECUTE('
IF OBJECT_ID (''dbo.SEQUENCE_POS_AUTHORIZATION'') IS NOT NULL
	DROP TABLE dbo.SEQUENCE_POS_AUTHORIZATION
')

EXECUTE('
CREATE TABLE dbo.SEQUENCE_POS_AUTHORIZATION
	(
	nada INT,
	id   NUMERIC (18) IDENTITY NOT NULL,
	CONSTRAINT PK_SEQUENCE_POS_AUTHORIZATION PRIMARY KEY (id)
	)
')

EXECUTE('
ALTER PROCEDURE [getSECUENCE_POS_AUTHORIZATION] 
		    @retorno numeric(18,0) out
	AS
	BEGIN
	    SET NOCOUNT ON;
		insert into SEQUENCE_POS_AUTHORIZATION (nada) values (0)
		set @retorno = (select @@identity as IDENT) 
		delete from SEQUENCE_POS_AUTHORIZATION where id = @retorno
	END
')
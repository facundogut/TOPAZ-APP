BEGIN
 SET @EXCEDE_GRAD = ''S'';
 SET @MENSAJE_INDIVIDUAL = ''Exceso en Graduación. Hay integrantes con una situación mayor a 1'';
 SET @MENSAJE_GRUPO = ''Exceso en Graduación. Hay integrantes con una situación mayor a 1'';
END*/

END
')
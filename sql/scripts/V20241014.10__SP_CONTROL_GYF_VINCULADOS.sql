EXECUTE('
ALTER PROCEDURE dbo.SP_CONTROL_GYF_VINCULADOS
@CLIENTE NUMERIC(12),
@PERSONA NUMERIC(12),
@TIPO_VINCULACION NUMERIC(3),
@IMPORTE_ANALISIS NUMERIC(15,2),
@TRAMO_CON_G NUMERIC(20,2),
@TRAMO_SIN_G NUMERIC(20,2),
@EXCEDE_GRAD VARCHAR(1) OUTPUT,
@MENSAJE_INDIVIDUAL VARCHAR(200) OUTPUT,
@MENSAJE_GRUPO VARCHAR(200) OUTPUT,
@TIPO_LIMITE VARCHAR(30) OUTPUT,
@IMPORTE_LIMITE NUMERIC(15,2) OUTPUT
AS
BEGIN
DECLARE @TABLA_VINCULADOS TABLE (CLIENTE NUMERIC(10), DEUDA_TOTAL NUMERIC(15,2), 
SITUACION VARCHAR(3), VINCULACION NUMERIC(3), PORC_SIN_G NUMERIC(5,2),
PORC_CON_G NUMERIC(5,2), PERSONA NUMERIC(12))
DECLARE @SIT_MAYOR NUMERIC(3)
DECLARE @TOPE_SUELDO NUMERIC(18,2)
DECLARE @DEUDA_TOTAL NUMERIC(18,2)
DECLARE @DEUDA_12M NUMERIC(18,2)
DECLARE @DEUDA_TOPE NUMERIC(20,2)
DECLARE @DEUDA_GRUPO NUMERIC(20,2)
DECLARE @PORCENTAJE_SING NUMERIC(5,2)
DECLARE @PORCENTAJE_CONG NUMERIC(5,2)
DECLARE @VINCULANTE_DIR NUMERIC(5)
DECLARE @DIRECTORES NUMERIC(5)
DECLARE @DEUDA_TOPE_SING NUMERIC(20,2)
DECLARE @DEUDA_TOPE_CONG NUMERIC(20,2)
DECLARE @CER_ACTUAL NUMERIC(11,4)
DECLARE @CER_FECHAINICIO NUMERIC(11,4)
DECLARE @FECHA_PROCESO DATETIME
DECLARE @FECHA_INICIO DATETIME
--- Tabla auxiliar de deudas
DECLARE @TABLA_ASISTENCIAS TABLE (CLIENTE NUMERIC(12), JTS_ASISTENCIA NUMERIC(10),
IMPORTE_DEUDA NUMERIC(15,2), TIENE_GARANTIA VARCHAR(1), TIPO_GARANTIA NUMERIC(2),
PRODUCTO NUMERIC(5), FAMILIA NUMERIC(10), PERSONA NUMERIC(12))

INSERT INTO @TABLA_VINCULADOS
SELECT V.[Cliente Vinculado], V.[Deuda Total], V.Situacion, V.[Tipo de vinculo],
ISNULL(VR.PORC_GRANDES_EXP_SING,(SELECT IMPORTE FROM CRE_PARAMETROS WITH(NOLOCK) WHERE CODIGO=606)),
ISNULL(VR.PORC_GRANDES_EXP_CNG,(SELECT IMPORTE FROM CRE_PARAMETROS WITH(NOLOCK) WHERE CODIGO=607)),
V.[Persona Vinculada]
FROM VW_VINCULACIONES_DEUDAS V WITH(NOLOCK) 
LEFT JOIN CRE_VINCULACIONES G WITH(NOLOCK) ON V.[ID Vinculo] = G.ID_VINCULO AND
G.TZ_LOCK = 0
LEFT JOIN CRE_VINCULOS_RELACIONES VR WITH(NOLOCK) ON VR.TIPO_RELACION = G.TIPO_RELACION AND VR.ID_VINCULO = G.TIPO_VINCULO AND VR.TZ_LOCK = 0
WHERE ([Cliente Vinculante] = @CLIENTE OR [Cliente Vinculado] = @CLIENTE)

	INSERT INTO @TABLA_ASISTENCIAS
	SELECT 
	AD.CLIENTE,
	AD.JTS_OID,
	SUM(AD.SALDO_DEUDA),
	CASE 
	WHEN gar.JTSOID_SALDO IS NULL THEN ''N''
	ELSE ''S''
	END AS GARANTIA,
	ISNULL(gar.TIPO_GARANTIA,0),
	p.C6250, 
	p.C6283,
	AD.NUMEROPERSONA
	FROM VW_DEUDA_CLIENTES AD WITH(NOLOCK)
	INNER JOIN SALDOS S WITH(NOLOCK) ON S.JTS_OID = AD.JTS_OID
	INNER JOIN PRODUCTOS p WITH(NOLOCK) ON s.PRODUCTO = p.C6250 
	AND ( (p.tz_lock < 300000000000000 OR p.tz_lock >= 400000000000000) 
	AND (p.tz_lock < 100000000000000 OR p.tz_lock >= 200000000000000)  )	
	LEFT JOIN (SELECT DISTINCT CG.JTSOID_SALDO, CTG.TIPO_GARANTIA FROM CRE_GARANTIACREDITO CG WITH(NOLOCK)
	LEFT JOIN CRE_GARANTIASRECIBIDAS CGR WITH(NOLOCK) ON CGR.NUM_GARANTIA = CG.NUM_GARANTIA
	LEFT JOIN CRE_TIPOSGARANTIAS CTG WITH(NOLOCK) ON CGR.TIPOGARANTIA = CTG.TIPO_GARANTIA AND
	CTG.TIPO_BCRA IN (1,2)) gar ON gar.JTSOID_SALDO = S.JTS_OID
	WHERE AD.SALDO_DEUDA <> 0 ---AND S.C1785 IN (1,5,6)
	AND AD.CLIENTE IN (SELECT CLIENTE FROM @TABLA_VINCULADOS) AND AD.NUMEROPERSONA = @PERSONA
	GROUP BY AD.JTS_OID, gar.JTSOID_SALDO, gar.TIPO_GARANTIA, p.C6250, p.C6283, AD.CLIENTE, AD.NUMEROPERSONA
	


SET @DIRECTORES = (SELECT IMPORTE FROM CRE_PARAMETROS WITH(NOLOCK) WHERE CODIGO = 620)

SET @DEUDA_GRUPO = (SELECT SUM(IMPORTE_DEUDA) FROM @TABLA_ASISTENCIAS)

SET @SIT_MAYOR = (SELECT COUNT(1) FROM @TABLA_VINCULADOS WHERE SITUACION > 1)

SET @FECHA_PROCESO = (SELECT FECHAPROCESO FROM PARAMETROS WITH(NOLOCK))

SET @CER_ACTUAL = (SELECT TOP 1 CER FROM CRE_GRAD_CER WITH(NOLOCK) WHERE FECHA <= @FECHA_PROCESO ORDER BY FECHA DESC)

SET @FECHA_INICIO = (SELECT TOP 1 FECHA_INICIO FROM CRE_VINCULACIONES WITH(NOLOCK) WHERE ID_PERSONA_VINCULADA = @PERSONA AND TIPO_VINCULO = @TIPO_VINCULACION AND TZ_LOCK = 0)

SET @CER_FECHAINICIO = (SELECT TOP 1 CER FROM CRE_GRAD_CER WITH(NOLOCK) WHERE FECHA <= @FECHA_INICIO ORDER BY FECHA DESC)

SET @VINCULANTE_DIR = (SELECT COUNT(1) FROM CRE_VINCULACIONES WITH(NOLOCK) WHERE ID_PERSONA_VINCULADA = @PERSONA 
AND TIPO_VINCULO IN (SELECT ID_VINCULO FROM CRE_VINCULOS_RELACIONES WITH(NOLOCK) WHERE EVALUA_MAYOR_DEUDA = ''S'') 
AND ID_PERSONA_VINCULANTE IN (SELECT ID_PERSONA_VINCULADA FROM CRE_VINCULACIONES WITH(NOLOCK) WHERE TIPO_VINCULO = @DIRECTORES 
AND FECHA_INICIO <= (SELECT FECHAPROCESO FROM PARAMETROS WITH(NOLOCK))
AND (FECHA_FIN >= (SELECT FECHAPROCESO FROM PARAMETROS WITH(NOLOCK)) OR FECHA_FIN IS NULL)) AND TZ_LOCK = 0)

IF(@VINCULANTE_DIR > 0)
BEGIN
	SET @PORCENTAJE_SING = 0

	SET @PORCENTAJE_CONG = 0
END
ELSE
BEGIN
	SET @PORCENTAJE_SING = (SELECT PORC_GRANDES_EXP_SING FROM CRE_VINCULOS_RELACIONES WITH(NOLOCK) WHERE ID_VINCULO = @TIPO_VINCULACION)

	SET @PORCENTAJE_CONG = (SELECT PORC_GRANDES_EXP_CNG FROM CRE_VINCULOS_RELACIONES WITH(NOLOCK) WHERE ID_VINCULO = @TIPO_VINCULACION)
END

IF(@PORCENTAJE_SING = 0 AND @PORCENTAJE_CONG = 0)
 BEGIN
  SET @DEUDA_12M = ((SELECT MAYOR_DEUDA_12M_PREV FROM CRE_VINCULACIONES WITH(NOLOCK) WHERE ID_PERSONA_VINCULADA = @PERSONA AND TIPO_VINCULO = @TIPO_VINCULACION AND TZ_LOCK = 0)*@CER_ACTUAL)/@CER_FECHAINICIO
  
  SET @TOPE_SUELDO = ((SELECT IMPORTE FROM CRE_PARAMETROS WITH(NOLOCK) WHERE CODIGO = 611) * 
  					 (SELECT IMPORTE FROM CRE_PARAMETROS WITH(NOLOCK) WHERE CODIGO = 612))
  IF(@DEUDA_12M>@TOPE_SUELDO)
   BEGIN
    SET @TOPE_SUELDO = @DEUDA_12M; 
    SET @TIPO_LIMITE = ''Mayor deuda''
   END
  ELSE
   BEGIN
    SET @TIPO_LIMITE = (SELECT CONVERT(VARCHAR,IMPORTE) FROM CRE_PARAMETROS WITH(NOLOCK) WHERE CODIGO = 612)+''* SMVM''
   END
   
   SET @DEUDA_TOPE = @TOPE_SUELDO;
   
   SET @DEUDA_TOTAL = (SELECT ISNULL(SUM(IMPORTE_DEUDA),0) FROM @TABLA_ASISTENCIAS WHERE PERSONA = @PERSONA)
   
   SET @IMPORTE_LIMITE = @DEUDA_TOPE;
 END
 ELSE
 BEGIN
    SET @DEUDA_TOTAL = (SELECT ISNULL(SUM(IMPORTE_DEUDA),0) FROM @TABLA_ASISTENCIAS WHERE PERSONA = @PERSONA)
    
 	SET @DEUDA_TOPE_SING = ((SELECT IMPORTE FROM CRE_PARAMETROS WITH(NOLOCK) WHERE CODIGO = 600)*@PORCENTAJE_SING)/100;
 	
 	SET @DEUDA_TOPE_CONG = ((SELECT IMPORTE FROM CRE_PARAMETROS WITH(NOLOCK) WHERE CODIGO = 600)*@PORCENTAJE_CONG)/100;
 	
 	SET @TIPO_LIMITE = '''';
 	
 	SET @IMPORTE_LIMITE = 0;
 	
 	/*(((SELECT IMPORTE FROM CRE_PARAMETROS WITH(NOLOCK) WHERE CODIGO = 617)*
 	(SELECT IMPORTE FROM CRE_PARAMETROS WITH(NOLOCK) WHERE CODIGO = 618))*
 	(SELECT IMPORTE FROM CRE_PARAMETROS WITH(NOLOCK) WHERE CODIGO = 616))/100;*/
 END
 
 
 IF(@PORCENTAJE_SING = 0 AND @PORCENTAJE_CONG = 0)
 BEGIN
 	IF((@DEUDA_TOTAL+@IMPORTE_ANALISIS)>@DEUDA_TOPE)
 	BEGIN
 		SET @EXCEDE_GRAD = ''S'';
 		SET @MENSAJE_INDIVIDUAL = ''Exceso en Grandes Exposiciones: ''+CONVERT(VARCHAR(30),FORMAT(ROUND((@DEUDA_TOTAL+@IMPORTE_ANALISIS)-@DEUDA_TOPE,2), ''C'', ''es-ar''))+''.'';
 	END
 	ELSE 
 	BEGIN
 		SET @MENSAJE_INDIVIDUAL = ''Encuadrado'';
 		SET @EXCEDE_GRAD = ''N'';
 	END
 END
 
 IF(@PORCENTAJE_SING = 0 AND @PORCENTAJE_CONG > 0)
 BEGIN
 	IF((@TRAMO_CON_G+@TRAMO_SIN_G)>@DEUDA_TOPE_CONG)
 	BEGIN
 		SET @EXCEDE_GRAD = ''S'';
 		SET @MENSAJE_INDIVIDUAL = ''Exceso en Grandes Exposiciones con y sin garantía: ''+CONVERT(VARCHAR(30),FORMAT(ROUND((@TRAMO_CON_G+@TRAMO_SIN_G)-@DEUDA_TOPE_CONG,2), ''C'', ''es-ar''))+''.'';
 	END
 	ELSE 
 	BEGIN
 		SET @MENSAJE_INDIVIDUAL = ''Encuadrado'';
 		SET @EXCEDE_GRAD = ''N'';
 	END
 END
 
 IF(@PORCENTAJE_SING > 0 AND @PORCENTAJE_CONG = 0)
 BEGIN
 	IF(@TRAMO_SIN_G>@DEUDA_TOPE_SING)
 	BEGIN
 		SET @EXCEDE_GRAD = ''S'';
 		SET @MENSAJE_INDIVIDUAL = ''Exceso en Grandes Exposiciones sin garantía: ''+CONVERT(VARCHAR(30),FORMAT(ROUND((@TRAMO_SIN_G)-@DEUDA_TOPE_SING,2), ''C'', ''es-ar''))+''.'';
 	END
 	ELSE 
 	BEGIN
 		SET @MENSAJE_INDIVIDUAL = ''Encuadrado'';
 		SET @EXCEDE_GRAD = ''N'';
 	END
 END
 
 IF(@PORCENTAJE_SING > 0 AND @PORCENTAJE_CONG > 0)
 BEGIN
 	IF((@TRAMO_SIN_G)>@DEUDA_TOPE_SING AND (@TRAMO_CON_G+@TRAMO_SIN_G)<=@DEUDA_TOPE_CONG)
 	BEGIN
 		SET @EXCEDE_GRAD = ''S'';
 		SET @MENSAJE_INDIVIDUAL = ''Exceso en Grandes Exposiciones sin garantía: ''+CONVERT(VARCHAR(30),FORMAT(ROUND((@TRAMO_SIN_G)-@DEUDA_TOPE_SING,2), ''C'', ''es-ar''))+char(10) + char(13) + ''Encuadrado en Grandes Exposiciones con y sin garantía.'';
 	END
 	ELSE IF((@TRAMO_SIN_G)<=@DEUDA_TOPE_SING AND (@TRAMO_CON_G+@TRAMO_SIN_G)>@DEUDA_TOPE_CONG)
 	BEGIN
		SET @MENSAJE_INDIVIDUAL = ''Encuadrado en Grandes Exposiciones sin garantía.'' +char(10) + char(13) + ''Exceso en Grandes Exposiciones con y sin garantía: '' + CONVERT(VARCHAR(30),FORMAT(ROUND((@TRAMO_CON_G+@TRAMO_SIN_G)-@DEUDA_TOPE_CONG,2), ''C'', ''es-ar'')) + ''.'';
		SET @EXCEDE_GRAD = ''S'';
	END	
	ELSE IF((@TRAMO_SIN_G)>@DEUDA_TOPE_SING AND (@TRAMO_CON_G+@TRAMO_SIN_G)>@DEUDA_TOPE_CONG)
 	BEGIN
		SET @MENSAJE_INDIVIDUAL = ''Exceso en Grandes Exposiciones sin garantía: '' + CONVERT(VARCHAR(30),FORMAT(ROUND(@TRAMO_SIN_G-@DEUDA_TOPE_SING,2), ''C'', ''es-ar'')) + ''.'' +char(10) + char(13) + ''Exceso en Grandes Exposiciones con y sin garantía: '' + CONVERT(VARCHAR(30),FORMAT(ROUND((@TRAMO_CON_G+@TRAMO_SIN_G)-@DEUDA_TOPE_CONG,2), ''C'', ''es-ar'')) + ''.'';
		SET @EXCEDE_GRAD = ''S'';
	END	
 	ELSE IF((@TRAMO_SIN_G)<=@DEUDA_TOPE_SING AND (@TRAMO_CON_G+@TRAMO_SIN_G)<=@DEUDA_TOPE_CONG) 
 	BEGIN
 		SET @MENSAJE_INDIVIDUAL = ''Encuadrado en Grandes Exposiciones sin garantía.'' +char(10) + char(13) + ''Encuadrado en Grandes Exposiciones con y sin garantía.'';
 		SET @EXCEDE_GRAD = ''N'';
 	END
 END
 
 
 
 /*IF(@DEUDA_GRUPO>@DEUDA_TOPE)
 BEGIN
 SET @MENSAJE_GRUPO = ''Exceso en Graduación: ''+CONVERT(VARCHAR(30),FORMAT(ROUND(@DEUDA_GRUPO-@DEUDA_TOPE,2), ''C'', ''es-ar''))+''.''; ---''Tope deuda = '' + CONVERT(VARCHAR, @DEUDA_TOPE) + '' Deuda grupo = ''+@DEUDA_GRUPO;
 END
 ELSE 
 SET @MENSAJE_GRUPO = ''Encuadrado'';*/


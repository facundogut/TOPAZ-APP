EXECUTE('
	IF OBJECT_ID (''dbo.VW_CHE_CHEQUES_PROCESO'') IS NOT NULL
		DROP VIEW dbo.VW_CHE_CHEQUES_PROCESO
')

EXECUTE('
CREATE VIEW VW_CHE_CHEQUES_PROCESO
AS
SELECT	
		ROW_NUMBER() OVER(ORDER BY CH.SUCURSAL,CH.MONEDA,CH.CUENTA,CH.PRODUCTO,CH.OPERACION,CH.ORDINAL,CH.NUMEROCHEQUE,CH.SERIE) AS IDENTIFICADOR,
		CH.SUCURSAL,
		CH.MONEDA,
		CH.CUENTA,
		CH.PRODUCTO,
		CH.OPERACION,
		CH.ORDINAL,
		CH.NUMEROCHEQUE,
		CH.SERIE,
		CH.ESTADO,
		CH.FECHA_VENCIMIENTO,
		CH.TZ_LOCK
FROM CHE_CHEQUES AS CH with (nolock)
WHERE ((CH.TZ_LOCK < 100000000000000 OR CH.TZ_LOCK >= 200000000000000)
		AND (CH.TZ_LOCK < 300000000000000 OR CH.TZ_LOCK >= 400000000000000))
')
EXECUTE('
ALTER PROCEDURE [dbo].[SP_BORRA_BITACORA_COBRADOR]
@SOPORTE NUMERIC(10),
@LOTE NUMERIC(10),
@CLIENTE NUMERIC(12),
@JTS_CV NUMERIC(10)
AS
BEGIN
	DELETE COB FROM CRE_COBRANZAS_ACREDITADOR COB
	WHERE SOPORTE = @SOPORTE AND LOTE = @LOTE 
	AND CODIGOCLIENTE = @CLIENTE AND JTS_CV = @JTS_CV
	AND NOT EXISTS (SELECT 1 FROM ASIENTOS WITH(NOLOCK) WHERE ASIENTO=COB.ASIENTO AND SUCURSAL=COB.SUCURSAL
	AND FECHAPROCESO=COB.FECHA)
END
')
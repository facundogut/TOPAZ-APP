EXEC('
	UPDATE TJD_TLF_SUMMARY 
	SET CONCILIACION=''N''
	WHERE 
		FECHACAPTURA >= ''0529''
		AND NOT EXISTS (
			select 1 
			from TLF_CONCILIACION_CABECERA
			where 	LKTRAD=FECHATRANSACCION
				and LKTRAT=HORATRANSACCION 
				and LKSEQN=NUMEROTRANSACCION 
				and LKTERM=IDENTIFICACIONCAJERO 
				and CODIGOPROCESAMIENTO=COD_MSJ_H2H
			)
');
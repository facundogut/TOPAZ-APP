EXECUTE ('
CREATE OR ALTER VIEW VW_CUENTAS_DPF_SIN_VTA_PROPIA
(
								CUENTA,
								NOMBRE,
								PRODUCTO,
								DESCRIPCION,
								MONEDA,
								SUCURSAL,
								OPERACION,
								ORDINAL,
								CLIENTE,
								JTS_OID,
								BLOQUEO,
								CANCELACION,
								CONTRACUENTA	) AS
SELECT 
    S.CUENTA, 
    C.NOMBRECLIENTE, 
    S.PRODUCTO, 
    P.C6251, 
    S.MONEDA, 
    S.SUCURSAL, 
    S.OPERACION, 
    S.ORDINAL, 
    S.C1803, 
    S.JTS_OID,
    CASE WHEN S.C1679 = ''1'' THEN ''Si'' ELSE ''No'' END AS BLOQUEO,
    S.C1651,
    S.C1665
FROM 
    SALDOS AS S WITH (NOLOCK)
INNER JOIN 
    PRODUCTOS AS P WITH (NOLOCK) 
    ON S.PRODUCTO = P.C6250 
    AND P.TZ_LOCK = 0 
INNER JOIN 
    CLI_CLIENTES C WITH (NOLOCK) 
    ON C.CODIGOCLIENTE = S.C1803 
    AND C.TZ_LOCK = 0 
INNER JOIN 
	MONEDAS M WITH (NOLOCK) 
	ON S.MONEDA = M.C6399
	
WHERE 
    S.TZ_LOCK = 0 
    AND S.C1785 = 4 
    AND S.C1604 <> 0
    AND S.C1608 <> 0
    AND M.C6403 <> ''I'' 
    AND C.EXCEPCION_CUENTA_VISTA = ''S''
    


')
EXECUTE('
CREATE OR ALTER PROCEDURE SP_OBTENER_COTIZACION_BNA
	@FECHA DATETIME2,
	@TC_COMPRA NUMERIC(15,9) OUTPUT,
	@TC_VENTA  NUMERIC(15,9) OUTPUT
AS 
BEGIN

	SELECT @TC_COMPRA=TC_COMPRA, @TC_VENTA= TC_VENTA  FROM CON_COTIZACIONES_BNA
WHERE Fecha_Cotizacion< @FECHA
  AND Fecha_Cotizacion = (SELECT max(Fecha_Cotizacion) FROM CON_COTIZACIONES_BNA 
  							WHERE Fecha_Cotizacion< @FECHA)
  							
	
END
')

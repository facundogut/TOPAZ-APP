EXECUTE('
	IF OBJECT_ID (''dbo.VW_PARAM_TASA_FIJA_DPF'') IS NOT NULL
		DROP VIEW dbo.VW_PARAM_TASA_FIJA_DPF;
')		

EXECUTE('
	CREATE VIEW [dbo].[VW_PARAM_TASA_FIJA_DPF] AS
	SELECT 
		DPF.PRODUCTO AS Producto,  
		DPF.MONEDA AS Moneda,  
		DPF.SECTOR AS Sector,  
		DPF.PUNTOS_PORCENTUALES AS Puntos_Porcentuales
	FROM
		DPF_PARAM_TASA_PRECANCELACION DPF
')

EXECUTE('
	IF OBJECT_ID (''dbo.VW_CLI_SECTOR'') IS NOT NULL
		DROP VIEW dbo.VW_CLI_SECTOR;
')

EXECUTE('
	CREATE VIEW [dbo].[VW_CLI_SECTOR] AS
	SELECT 
		SEC.SECTOR AS Sector,  
		SEC.SECTOR_CONTABLE AS Sector_Contable,  
		SEC.DESCRIPCION AS Descripción
	FROM
		CLI_SECTORES SEC
')

EXECUTE('
	IF OBJECT_ID (''dbo.VW_MONEDA_NO_INDEXADA'') IS NOT NULL
		DROP VIEW dbo.VW_MONEDA_NO_INDEXADA;
')

EXECUTE('
	CREATE VIEW [dbo].[VW_MONEDA_NO_INDEXADA] AS
	SELECT 
		MON.C6399 AS Código_Moneda,  
		MON.C6400 AS Moneda
	FROM
		MONEDAS MON
	WHERE C6399 BETWEEN 1 AND 6
')
     	      FROM CLI_PAISES CP
     	      WHERE CP.CODIGOPAIS=CPF.PAISRESIDENCIA)
    END AS RESIDENCIA, 
    CASE 
        WHEN CCP.TITULARIDAD = ''T'' THEN 1
        ELSE 2

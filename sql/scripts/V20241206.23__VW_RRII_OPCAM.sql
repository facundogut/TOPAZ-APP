            END
        ELSE NULL
    END AS CODIGO_INSTRUMENTO_VC
    
    FROM MOVIMIENTOS_CONTABLES MC WITH(NOLOCK)
    INNER JOIN CLI_CLIENTEPERSONA CCP WITH(NOLOCK) ON CCP.CODIGOCLIENTE=MC.CLIENTE AND CCP.TZ_LOCK=0

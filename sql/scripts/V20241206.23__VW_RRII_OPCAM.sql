            END
      
        WHEN CO.CODIGO_OPERACION IN (3016, 3062) THEN 
            CASE 
                WHEN TCT.CODIGO_TRANSACCION = 1504 THEN 
                    CASE 
                        WHEN MC.MONEDA =1 THEN 01 
                        ELSE 10 
                    END
                WHEN TCT.CODIGO_TRANSACCION = 1503 THEN 07 
                ELSE NULL
            END
        ELSE NULL

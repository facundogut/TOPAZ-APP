    ''20240822'' AS FECHA_INFORMACION,--PARAMETRO
    CASE 
        WHEN EXISTS (SELECT 1 FROM SUCURSALES S WHERE S.ZONACOBRO = 1 AND S.SUCURSAL=MC.SUCURSAL) THEN ''1''
        ELSE ''6''
    END AS CODIGO_JURISDICCION,

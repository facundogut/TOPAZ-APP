

EXECUTE('

CREATE OR ALTER VIEW VW_REL_TARJETA_CTA_ACTIVAS AS
SELECT 
		R.ID_TARJETA AS ID_TARJETA ,
		R.SALDO_JTS_OID AS SALDO_JTS_OID,
		R.ESTADO AS ESTADO
	
FROM TJD_REL_TARJETA_CUENTA R
LEFT JOIN TJD_TMP_MODIF_TARJETA_CUENTA T 
  ON R.ID_TARJETA = T.ID_TARJETA 
  AND R.SALDO_JTS_OID = T.SALDO_JTS_OID
  WHERE T.ID_TARJETA IS NULL;



')
EXECUTE('
IF OBJECT_ID (''dbo.VW_CARGO_CTAVTA_INMOVILIZADAS'') IS NOT NULL
	DROP VIEW dbo.VW_CARGO_CTAVTA_INMOVILIZADAS
')
EXECUTE('
CREATE   VIEW [dbo].[VW_CARGO_CTAVTA_INMOVILIZADAS](
															JTS_OID_COBRO,
															MONEDA,
															SALDO_ACTUAL,
															DIAS_NOTIFICADO,
															DIAS_PARAMETRO,
															USF)
AS 
SELECT	S.JTS_OID, 
		S.MONEDA, 
		S.C1604+S.C1683+S.C1605+S.C2627+S.C50107, 
		DATEDIFF(day,VH.FECHA_INMOVILIZACION , P.FECHAPROCESO ), 
		PG.NUMERICO, 
		C.USF
FROM	PARAMETROSGENERALES PG with(nolock), 
		PARAMETROS P with(nolock), 
		SALDOS S with(nolock)
		INNER JOIN VTA_HISTORICO_INMOVILIZADAS VH with(nolock) ON S.JTS_OID = VH.JTS_OID_SALDO
		INNER JOIN CLI_CLIENTES C with(nolock) ON C.[CODIGOCLIENTE] = S.C1803
WHERE   S.C1785 IN (2,3)
        AND S.TIPO_SALDO_FONDO=0
		AND ((S.TZ_LOCK < 300000000000000 OR S.TZ_LOCK >= 400000000000000) 
				AND (S.TZ_LOCK < 100000000000000 OR S.TZ_LOCK >= 200000000000000))
		AND S.C1728 = ''I''
		AND PG.[CODIGO] =222
		AND VH.ESTADO_CTA = ''I'';
')

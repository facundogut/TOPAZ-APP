EXECUTE('
CREATE OR ALTER PROCEDURE SP_CRE_ASISTENCIAS_CIERRE

    @RESULTADO INT OUTPUT
AS
BEGIN
    SET NOCOUNT ON;
    
    -- Verificamos si en PARAMETROS tiene MARCAEOD = ''I''
    SELECT @RESULTADO = CASE WHEN MARCAEOD = ''I'' 
    					THEN 1 
    					ELSE 0 
    					END FROM PARAMETROS WITH (NOLOCK);
END;

')
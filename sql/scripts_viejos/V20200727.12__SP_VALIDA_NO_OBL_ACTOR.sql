EXECUTE('CREATE PROCEDURE SP_VALIDA_NO_OBL_ACTOR
@pRol VARCHAR(3),
@pValido NUMERIC(1) OUTPUT
AS
DECLARE
	@vContCausas NUMERIC(6)
BEGIN
	SET @pValido = 1
	
	SELECT @vContCausas = COUNT(*)
	FROM DJ_CAUSAS C
	WHERE C.ESTADO=''A'' AND C.TZ_LOCK=0
	AND C.NRO_CAUSA NOT IN (SELECT IC.NRO_CAUSA FROM DJ_INTEGRANTES_CAUSAS IC WHERE IC.ACTOR=@pRol AND IC.TZ_LOCK=0) 
	
	IF @vContCausas > 0
	BEGIN
		SET @pValido = 0
	END
END
');
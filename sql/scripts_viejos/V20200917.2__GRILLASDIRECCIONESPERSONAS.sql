CREATE VIEW dbo.VW_EMAILS  (
	TIPODOCUMENTO,
	NUMERODOCUMENTO,
	TIPO_DOC_FISICO,
	NUM_DOC_FISICO,
	NOMBRE		 ,
	ID           ,
	FORMATO      ,
	DESC_FORMATO ,
	TIPO         ,
	DESC_TIPO    ,
	ORDINAL      ,
	EMAIL       
)
AS
 SELECT 
    v.TIPODOCUMENTO,
    v.NUMERODOCUMENTO,
    v.TIPO_DOC_FISICO,
    v.NUM_DOC_FISICO,
 	concat(u.APELLIDOPATERNO,' ',u.PRIMERNOMBRE) AS NOMBRE,

   	e.ORDINAL,
	e.EMAIL
    
         
   FROM  
      dbo.CLI_EMAILS AS E,
      dbo.CLI_PERSONASFISICAS  AS U, 
      dbo.CLI_DOCUMENTOSPFPJ  AS V
      

   	e.FORMATO,
   	(SELECT DESCRIPCION FROM OPCIONES O WHERE O.IDIOMA = 'E' AND O.NUMERODECAMPO = 33460 AND O.OPCIONINTERNA= e.FORMATO) AS DESC_FORMATO,

      (U.NUMEROPERSONAFISICA = E.ID AND U.NUMEROPERSONAFISICA = V.NUMEROPERSONAFJ) AND 
      ((U.TZ_LOCK < 300000000000000 OR U.TZ_LOCK >= 400000000000000) AND (U.TZ_LOCK < 100000000000000 OR U.TZ_LOCK >= 200000000000000)) AND 
      ((V.TZ_LOCK < 300000000000000 OR V.TZ_LOCK >= 400000000000000) AND (V.TZ_LOCK < 100000000000000 OR V.TZ_LOCK >= 200000000000000)) AND
      ((E.TZ_LOCK < 300000000000000 OR E.TZ_LOCK >= 400000000000000) AND (E.TZ_LOCK < 100000000000000 OR E.TZ_LOCK >= 200000000000000))
UNION ALL
  SELECT 
    v.TIPODOCUMENTO,
    v.NUMERODOCUMENTO,
    v.TIPO_DOC_FISICO,
    v.NUM_DOC_FISICO,
 	j.RAZONSOCIAL AS NOMBRE,

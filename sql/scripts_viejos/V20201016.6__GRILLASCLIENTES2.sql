EXECUTE ('
ALTER VIEW dbo.VW_PF_HAB_DOCUMENTOS (
   "Tipo Documento Identificativo", 
   "Número Documento Identificativo",
   "Tipo Documento Físico",
   "Número de Documento Físico",
   "Apellidos y Nombres",
   "Tipo de Persona", 
   "Nivel de Apertura",
   "Estado de la Persona",
   "Motivo Inhabilitado",
   "Número de Persona")
AS 
   SELECT TOP 9223372036854775807 WITH TIES 
      V.TIPODOCUMENTO , 
      V.NUMERODOCUMENTO ,
      V.TIPO_DOC_FISICO ,
      V.NUM_DOC_FISICO ,
      (U.APELLIDOPATERNO +'' ''+U.PRIMERNOMBRE) AS NOMBRES,
      V.TIPOPERSONA ,  
      (SELECT DESCRIPCION FROM OPCIONES O WHERE O.IDIOMA=''E'' AND O.OPCIONINTERNA=U.NIVEL_APERTURA AND O.NUMERODECAMPO = 2203) AS NIVEL_APERTURA,
      (SELECT DESCRIPCION FROM OPCIONES O WHERE O.IDIOMA=''E'' AND O.OPCIONINTERNA=U.ESTADO AND O.NUMERODECAMPO = 33366) AS ESTADO,   
	  (SELECT DESCRIPCION FROM OPCIONES O WHERE O.IDIOMA=''E'' AND O.OPCIONINTERNA=U.MOTIVO_INHABILITADO AND O.NUMERODECAMPO = 33368) AS  MOTIVO_INHABILITADO,
      V.NUMEROPERSONAFJ 
       
   FROM dbo.CLI_PERSONASFISICAS  AS U, dbo.CLI_DOCUMENTOSPFPJ  AS V
   WHERE 
      U.NUMEROPERSONAFISICA = V.NUMEROPERSONAFJ AND 
      (V.TIPOPERSONA =''F'') AND 
      (U.TZ_LOCK =0) AND 
      (V.TZ_LOCK =0)
   ORDER BY NUMEROPERSONAFJ

')


EXECUTE('CREATE PROCEDURE SP_DJ_CARGA_PODERES_TEMP
@pUsuario VARCHAR(8),
@pTipoDocumento VARCHAR(4),
@pNroDocumento VARCHAR(12),
@pNombrePersona VARCHAR(100),
@pPoder VARCHAR(100)
AS
BEGIN
	DELETE FROM DJ_TEMP_PODERES WHERE USUARIO=@pUsuario AND TIPO_DOCUMENTO=@pTipoDocumento AND NRO_DOCUMENTO=@pNroDocumento AND PODER=@pPoder
	
	INSERT INTO DJ_TEMP_PODERES (USUARIO, TIPO_DOCUMENTO, NRO_DOCUMENTO, NOMBRE_PERSONA, PODER)
	VALUES (@pUsuario, @pTipoDocumento, @pNroDocumento, @pNombrePersona, @pPoder)
END
');
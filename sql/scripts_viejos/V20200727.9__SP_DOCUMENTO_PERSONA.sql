EXECUTE('CREATE PROCEDURE SP_DOCUMENTO_PERSONA
@pNroPersona NUMERIC(12),
@pTipoDocumento VARCHAR(4) OUT,
@pNroDocumento VARCHAR(20) OUT
AS
BEGIN
	SELECT TOP 1 @pTipoDocumento=D.TIPODOCUMENTO, @pNroDocumento=D.NUMERODOCUMENTO FROM CLI_DocumentosPFPJ D WHERE D.NUMEROPERSONAFJ=@pNroPersona AND D.TZ_LOCK=0
END
');
ALTER PROCEDURE SP_VALIDA_NO_OBL_ACTOR
@pRol VARCHAR(3),
@pValido NUMERIC(1) OUTPUT
AS
DECLARE
	@vContCausas NUMERIC(6)
BEGIN
	SET @pValido = 1
	
	SELECT @vContCausas = COUNT(*)
	FROM DJ_JUZGADOS j
	WHERE j.TZ_LOCK = 0
	AND j.NRO_JUZGADO NOT IN (SELECT Ij.NRO_JUZGADO FROM DJ_INTEGRANTES_JUZGADOS Ij WHERE ij.ACTOR=@pRol AND Ij.TZ_LOCK=0) 
	
	IF @vContCausas > 0
	BEGIN
		SET @pValido = 0
	END
END
GO
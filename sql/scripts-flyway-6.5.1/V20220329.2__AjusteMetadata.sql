						WHERE cen.COD_ENTIDAD <> right(''00000'' + CAST((SELECT NUMERICO FROM PARAMETROSGENERALES WITH (NOLOCK) WHERE CODIGO = 2) AS VARCHAR(5)),5) AND cen.TZ_LOCK = 0  AND

EXECUTE ('
CREATE OR ALTER VIEW dbo.VW_PJ_DOCUMENTOS_HAB ( 
										TIPODOCIDENT,
										NUMERODOCIDENT,
										RAZONSOCIAL,
										TIPOPERSONA,
										NIVEL_APERTURA,
										ESTADO,
										MOTIVO_INHABILITADO,
										NUMEROPERSONAFJ,
										TZ_LOCK
					  				   )
AS					  				   
(SELECT 
    V.TIPODOCUMENTO , 
    V.NUMERODOCUMENTO ,
    (U.RAZONSOCIAL) AS RAZONSOCIAL,
    V.TIPOPERSONA ,  
    (	SELECT DESCRIPCION FROM OPCIONES O WITH(NOLOCK) 
			WHERE O.IDIOMA=''E'' AND O.OPCIONINTERNA=U.NIVEL_APERTURA AND O.NUMERODECAMPO = 2045) AS NIVEL_APERTURA,
    (	SELECT DESCRIPCION FROM OPCIONES O WITH(NOLOCK) 
			WHERE O.IDIOMA=''E'' AND O.OPCIONINTERNA=U.ESTADO AND O.NUMERODECAMPO = 33367) AS ESTADO,   
	(	SELECT DESCRIPCION FROM OPCIONES O WITH(NOLOCK) 
			WHERE O.IDIOMA=''E'' AND O.OPCIONINTERNA=U.MOTIVO_INHABILITADO AND O.NUMERODECAMPO = 33369) AS  MOTIVO_INHABILITADO,
    V.NUMEROPERSONAFJ, 
    U.TZ_LOCK
FROM dbo.CLI_PERSONASJURIDICAS  AS U WITH(NOLOCK)
INNER JOIN dbo.CLI_DOCUMENTOSPFPJ  AS V WITH(NOLOCK) ON U.NUMEROPERSONAJURIDICA = V.NUMEROPERSONAFJ
													AND V.TIPOPERSONA = ''J'' 
													AND U.TZ_LOCK =0 
													AND V.TZ_LOCK =0

)
					  				   
')


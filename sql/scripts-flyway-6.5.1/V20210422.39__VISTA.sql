EXECUTE('

CREATE VIEW VW_DPF_CODIGOS_ESPECIE AS
SELECT
	DCOD.CODIGOESPECIE AS [Código_Especie],
	DCOD.DESCRIPCION AS [Descripción],
	DCOD.CATEGORIA AS [Categoría],
	DCAT.DESCRIPCION AS [Descripción_categoría],
	DCOD.TIPOTITULO AS [Tipo_título],
	DCOD.ESPECIE AS [Especie],
	DCOD.HABILITADO AS [Habilitado]
FROM
	DPF_CODIGOS_ESPECIE DCOD WITH (nolock)
INNER JOIN DPF_CATEGORIAS_ESPECIE DCAT WITH (nolock) ON
	DCOD.CATEGORIA = DCAT.CODIGOCATEGORIA
WHERE DCOD.TZ_LOCK = 0 AND DCAT.TZ_LOCK = 0

')



CREATE VIEW [dbo].[VW_SALDOS_INMOVILIZADOS_DPF] (
	SUCURSAL, 
	PRODUCTO, 
	MONEDA, 
	CLIENTES, 
	CUENTA, 
	OPERACION, 
	JTS_OID_COBRO,
	JTS_OID_SALDO,
	SALDOACTUAL, 
	FECHAINMOV,
	FECHA_PROCESO)
AS 

   SELECT 
      S.SUCURSAL AS SUCURSAL, 
      S.PRODUCTO AS PRODUCTO, 
      S.MONEDA AS MONEDA, 
      S.C1803 AS CLIENTES, 
      S.CUENTA AS CUENTA, 
      S.OPERACION AS OPERACION, 
      S.C1665 AS JTS_OID_COBRO,
      S.JTS_OID AS JTS_OID_SALDO,
      S.C1604 AS SALDOACTUAL, 
      S.C1626 AS FECHAINMOV,
         (
            SELECT PARAMETROS.FECHAPROCESO
            FROM PARAMETROS with (nolock)
         ) AS FECHA_PROCESO
   FROM 
      SALDOS  AS S with (nolock)
   WHERE 
      S.TZ_LOCK = 0 AND 
      S.C1785 = 4 AND 
      S.C1604 > 0 AND
      S.C1728 = 'I'
GO



EXECUTE('
---
IF OBJECT_ID (''dbo.VW_AYUDA_CONV_INFO'') IS NOT NULL
	DROP VIEW dbo.VW_AYUDA_CONV_INFO
---
')
EXECUTE('
---
CREATE   VIEW [dbo].[VW_AYUDA_CONV_INFO]
AS
SELECT C.Id_ConvRec AS "ID_CONVENIO", 
		C.NomConvRec AS "NOMBRE", 
		C.Cuit AS ''CUIT'', 
		C.Cliente AS "ID_CLIENTE", 
		CLI.NOMBRECLIENTE AS "NOMBRE_CLIENTE", 
		C.Id_TpoConv AS "ID_TIPO_CONVENIO", 
		T.DscTpoConv "DESCRIPCION_TIPO", 
		C.Canal AS ''CANAL'', 
		OC.Descripcion AS "DESCRIPCION_CANAL", 
		C.Estado AS ''ESTADO'',
		OE.DESCRIPCION AS "DESCRIPCION_ESTADO", 
		C.FecAlta AS "FECHA_ALTA", 
		C.FecVto AS "FECHA_VENCIMIENTO", 
		C.FecUltAct AS "FECHA_ULTIMA_ACTIVIDAD", 
		C.Id_RefExt AS "REF_EXTERNA", 
		C.Id_ConvPadre AS "ID_CONV_PADRE"
FROM CONV_CONVENIOS_REC C WITH(NOLOCK) 
LEFT JOIN CLI_CLIENTES CLI WITH(NOLOCK) ON C.Cliente = CLI.CODIGOCLIENTE AND CLI.TZ_LOCK = 0
JOIN CONV_TIPOS T WITH(NOLOCK) ON C.Id_TpoConv = T.Id_TpoConv AND T.TZ_LOCK = 0
JOIN OPCIONES OE WITH(NOLOCK) ON C.Estado = OE.OPCIONINTERNA AND OE.NUMERODECAMPO = 44756
JOIN OPCIONES OC WITH(NOLOCK) ON C.Canal = OC.OPCIONINTERNA AND OC.NUMERODECAMPO = 44750
WHERE C.TZ_LOCK = 0
---
')


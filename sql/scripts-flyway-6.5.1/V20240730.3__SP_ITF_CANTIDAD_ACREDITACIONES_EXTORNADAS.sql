execute('
CREATE PROCEDURE [dbo].[SP_ITF_CANTIDAD_ACREDITACIONES_EXTORNADAS]
   
   @P_ID_OPERACION  VARCHAR(50),
   @P_FECHAPROCESO  VARCHAR(8),
   @P_SATELITE 		VARCHAR(10),
   @P_CAUSAL 		VARCHAR(10),
   @O_CANTIDAD		NUMERIC(5) OUTPUT
    
AS 
  
   BEGIN
   		SELECT @O_CANTIDAD = COUNT(1) 
   		FROM ITF_ACREDITACION_BITACORA (NOLOCK)
   		WHERE 	ID_OPERACION = @P_ID_OPERACION 
   			AND FECHA_PROCESO = @P_FECHAPROCESO 
   			AND SATELITE = @P_SATELITE 
   			AND CAUSAL = @P_CAUSAL
   			AND TZ_LOCK > 0

   END
 ');
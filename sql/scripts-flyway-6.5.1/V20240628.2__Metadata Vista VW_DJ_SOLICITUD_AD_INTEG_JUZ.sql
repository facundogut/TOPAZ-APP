EXECUTE('
IF OBJECT_ID (''dbo.VW_DJ_SOLICITUD_AD_INTEG_JUZ'') IS NOT NULL
	DROP VIEW dbo.VW_DJ_SOLICITUD_AD_INTEG_JUZ
')

 EXECUTE('
 
CREATE VIEW dbo.VW_DJ_SOLICITUD_AD_INTEG_JUZ
AS
SELECT
	A.NRO_SOLICITUD,
	A.ID_PERSONA,
	A.NRO_JUZGADO,
	A.ACTOR,
	A.FECHA_INTEGRACION,
	A.FECHA_CESE,
	O.Descripcion AS ACTIVO,
	OE.Descripcion AS ESTADO,
	A.FECHA_ESTADO,
	A.SUCURSAL
FROM dbo.DJ_SOLICITUD_AD_INTEG_JUZ AS A WITH (nolock)
INNER JOIN OPCIONES AS O WITH (nolock) ON
	O.NUMERODECAMPO = 35668
	AND O.IDIOMA = ''E''
INNER JOIN OPCIONES AS OE WITH (nolock) ON
	OE.NUMERODECAMPO = 35670
	AND OE.IDIOMA = ''E''
WHERE
	A.TZ_LOCK = 0
	
')
CREATE PROCEDURE SP_VENCIMIENTO_LISTA_DESCUENTO
    @NRO_LISTA NUMERIC(12,0), 
    @FECHA_VENCIMIENTO DATETIME OUTPUT
    
AS
BEGIN
   SELECT @FECHA_VENCIMIENTO=max(FECHA_ACREDITACION) 
   FROM CLE_CHEQUES_LISTA_DESCUENTO WITH(NOLOCK) WHERE NUMERO_DEPOSITO = @NRO_LISTA AND TZ_LOCK = 0		 				
END
')
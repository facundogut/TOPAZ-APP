EXECUTE('
UPDATE VTA_SALDOS 
SET CTA_REDLINK = TP.TIPO_CTA_REDLINK+CTA_REDLINK
FROM VTA_SALDOS VS WITH(NOLOCK)
INNER JOIN SALDOS S WITH(NOLOCK) ON S.JTS_OID=VS.JTS_OID_SALDO
INNER JOIN TOPESPRODUCTO TP WITH(NOLOCK)ON S.PRODUCTO = TP.CODPRODUCTO AND S.MONEDA=TP.MONEDA
WHERE VS.TZ_LOCK=0 AND S.TZ_LOCK=0 AND TP.TZ_LOCK=0
AND DATALENGTH(VS.CTA_REDLINK)<21
')
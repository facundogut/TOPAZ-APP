EXECUTE('CREATE OR ALTER VIEW VW_IB_ACUERDOS
(
COD_SUCURSAL,
NUM_CUENTA,
MODULO,
FECHA_VENC_ACUERDO,
NRO_ACUERDO,
TASA_NOMINAL_ACUERDO,
LIM_ACUERDO_PACTADO
) AS 
SELECT SG.SUCURSAL_ACUERDO AS COD_SUCURSAL,
	(SELECT SA.CUENTA FROM SALDOS SA WHERE SA.JTS_OID = SG.JTS_OID_SALDO AND SA.TZ_LOCK=0) AS NUM_CUENTA
	,(SELECT CASE WHEN SA.C1785 = 2 THEN ''CC'' ELSE ''CA'' END
		FROM SALDOS SA 
		WHERE SA.JTS_OID = SG.JTS_OID_SALDO AND SA.TZ_LOCK=0) AS MODULO
		,CONVERT(NUMERIC(8),CONVERT(VARCHAR, SG.FECHA_VENCIMIENTO, 112)) AS FECHA_VENC_ACUERDO
		,SG.NRO_AUTORIZACION AS NRO_ACUERDO
		,SG.TASA_DE_INTERESES AS TASA_NOMINAL_ACUERDO
		,SG.IMPORTE AS LIM_ACUERDO_PACTADO
FROM VTA_SOBREGIROS SG 
WHERE SG.TZ_LOCK=0')

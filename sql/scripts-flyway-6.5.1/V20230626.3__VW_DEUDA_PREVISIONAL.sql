EXECUTE('
IF OBJECT_ID (''dbo.VW_DEUDA_PREVISIONAL'') IS NOT NULL
	DROP VIEW dbo.VW_DEUDA_PREVISIONAL
')

EXECUTE('

CREATE VIEW VW_DEUDA_PREVISIONAL AS(
SELECT TOP 99.999999  PERCENT CUIT,
 UPPER(SERVICIO) AS ''TIPO CONSULTA'',
 CASE WHEN TIENE_DEUDA = ''TRUE'' THEN ''SI'' ELSE ''NO'' END DEUDA, 
 DATEADD(dd, 0, DATEDIFF(dd, 0, FECHAPROCESO)) AS ''FECHA CONSULTA'',
 CONVERT(varchar,FECHAPROCESO,24) AS ''HORA CONSULTA''    
 FROM ITF_AFIP_DEUDA_CONSULTA_SERVICIO
 ORDER BY ''FECHA CONSULTA'' DESC,''HORA CONSULTA'' DESC     
) 
')
EXECUTE('
IF OBJECT_ID (''dbo.VW_RECHAZO_SOL_CANJE_INT'') IS NOT NULL
	DROP VIEW dbo.VW_RECHAZO_SOL_CANJE_INT
')
EXECUTE('
CREATE VIEW VW_RECHAZO_SOL_CANJE_INT
AS
SELECT concat(NRO_SOLICITUD, NRO_CHEQUE, SALDO_JTS_OID)AS NROSOLICITUD,
       NRO_SOLICITUD, 
	   NRO_CHEQUE, 
	   SERIE, 
	   SALDO_JTS_OID, 
	   IMPORTE, ESTADO, 
	   JTS_OID_DEPOSITO,
	   CONTABILIZADO
  FROM che_solicitud_canje_interno
 WHERE TZ_LOCK = 0 
   AND ESTADO IN (''R'',''T'') 
   AND OPERATIVA = ''D''
   AND (CONTABILIZADO!=''S'' OR CONTABILIZADO IS NULL)
')
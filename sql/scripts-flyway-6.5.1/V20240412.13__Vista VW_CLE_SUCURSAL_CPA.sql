EXECUTE('
IF OBJECT_ID (''dbo.VW_CLE_SUCURSAL_CPA'') IS NOT NULL
	DROP VIEW dbo.VW_CLE_SUCURSAL_CPA
')
EXECUTE('
CREATE VIEW VW_CLE_SUCURSAL_CPA
AS
SELECT
	S.ID AS SUCURSAL,
	S.ORDINAL,
	U.NOMBRESUCURSAL,
	S.CPA_VIEJO AS COD_POSTAL
FROM CLE_SUCURSAL_CPA AS S WITH (nolock)
INNER JOIN SUCURSALES AS U WITH (nolock) ON
	U.SUCURSAL = S.ID
	AND ((U.TZ_LOCK < 100000000000000 OR U.TZ_LOCK >= 200000000000000)
		AND (U.TZ_LOCK < 300000000000000 OR U.TZ_LOCK >= 400000000000000)  
	)	
WHERE
	S.TZ_LOCK = 0
')

EXECUTE('
IF OBJECT_ID (''dbo.VW_TIPOS_DOMINIO'') IS NOT NULL
	DROP VIEW dbo.VW_TIPOS_DOMINIO
')

EXECUTE('
CREATE   VIEW [dbo].[VW_TIPOS_DOMINIO] 
AS
SELECT O.OPCIONINTERNA AS OPCION, O.DESCRIPCION
FROM OPCIONES O WITH(NOLOCK)
WHERE O.NUMERODECAMPO = 45369
')

EXECUTE('
IF OBJECT_ID (''dbo.VW_TIPOS_SUBDOMINIO'') IS NOT NULL
	DROP VIEW dbo.VW_TIPOS_SUBDOMINIO
')

EXECUTE('
CREATE   VIEW [dbo].[VW_TIPOS_SUBDOMINIO] 
AS
SELECT O.OPCIONINTERNA AS OPCION, O.DESCRIPCION
FROM OPCIONES O WITH(NOLOCK)
WHERE O.NUMERODECAMPO = 45373
')

EXECUTE('
IF OBJECT_ID (''dbo.VW_DOMINIOS'') IS NOT NULL
	DROP VIEW dbo.VW_DOMINIOS
')

EXECUTE('
CREATE   VIEW [dbo].[VW_DOMINIOS] 
AS
SELECT conv.ID_CONVENIO, conv.ID_DOMINIO, conv.DESCRIPCION
FROM CONV_DOMINIOS conv WITH(NOLOCK)
GROUP BY conv.ID_CONVENIO, conv.ID_DOMINIO, conv.DESCRIPCION
')




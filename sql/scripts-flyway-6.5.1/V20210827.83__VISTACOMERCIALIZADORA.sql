EXECUTE('
IF OBJECT_ID (''dbo.VTA_COMERCIALIZADORAS'') IS NOT NULL
	DROP VIEW dbo.VTA_COMERCIALIZADORAS
')

EXECUTE('
CREATE VIEW dbo.VTA_COMERCIALIZADORAS (
ID,
DESCRIPCION
)
AS
SELECT NUMERICO, ALFA FROM PARAMETROSGENERALES WHERE [CODIGO] = 728
UNION
SELECT
P.ID_ConvPago,
P.NomConvPago
FROM CONV_CONVENIOS_PAG P
INNER JOIN CONV_TIPOS T ON T.Id_TpoConv = P.Id_TpoConv AND T.TpoProducto = ''M''
WHERE T.TZ_LOCK = 0 AND P.TZ_LOCK = 0
')
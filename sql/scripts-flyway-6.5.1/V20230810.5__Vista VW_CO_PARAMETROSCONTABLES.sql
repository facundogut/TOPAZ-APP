EXECUTE('
----
IF OBJECT_ID (''dbo.VW_CO_PARAMETROSCONTABLES'') IS NOT NULL
	DROP VIEW dbo.VW_CO_PARAMETROSCONTABLES
----
')
EXECUTE('
----
CREATE VIEW VW_CO_PARAMETROSCONTABLES
AS
SELECT
	ROW_NUMBER() OVER(ORDER BY
						P.FECHAULTIMOBALANCE,
						P.FECHAPROXIMOBALANCE,
						P.PERIODOBALANCE,
						P.FECHAEJECUCIONCIERRE)
	AS NRO_REGISTRO,
	P.FECHAULTIMOBALANCE,
	P.FECHAPROXIMOBALANCE,
	P.PERIODOBALANCE,
	P.FECHAEJECUCIONCIERRE
FROM CO_PARAMETROSCONTABLES AS P WITH (nolock)
----
')


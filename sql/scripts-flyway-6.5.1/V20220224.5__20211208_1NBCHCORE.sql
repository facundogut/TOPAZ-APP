-- Trigger para seleccionar los movimientos de ajuste que iran al Extendido
-- HM begin sentence
CREATE TRIGGER TG_ASIENTOS_EXTENDIDO ON movimientos_contables
AFTER INSERT
AS
BEGIN
DECLARE
@newAsiento numeric(10, 0),
@newSucursal numeric(10, 0),
@newFechaproceso DATETIME,
@newMarcaajuste VARCHAR (1),
@newFechavalor DATETIME
SELECT @newAsiento = Asiento FROM inserted
SELECT @newSucursal = Sucursal FROM inserted
SELECT @newFechaproceso = Fechaproceso FROM inserted
SELECT @newMarcaajuste = Marcaajuste FROM inserted
SELECT @newFechavalor = Fechavalor FROM inserted

IF ( @newMarcaajuste = 'A' AND
@newFechavalor < @newFechaproceso AND
DATEPART(month,@newFechavalor) <> DATEPART(month,@newFechaproceso)
)
BEGIN

IF NOT EXISTS (SELECT * FROM con_asientos_extendido
   WHERE sucursal = @newSucursal
   AND Asiento = @newAsiento
   AND FechaProceso = @newFechaproceso)
BEGIN
INSERT INTO con_asientos_extendido (sucursal, Asiento, FechaProceso)
VALUES (@newSucursal, @newAsiento, @newFechaproceso)
END
END
END
-- HM end sentence;
GO




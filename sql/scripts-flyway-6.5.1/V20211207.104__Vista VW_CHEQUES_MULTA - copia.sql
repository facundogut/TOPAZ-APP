EXECUTE('
CREATE VIEW VW_CHEQUES_MULTA ([Número de cheque],Sucursal,Cuenta,Denominación,Producto,Moneda,Importe,[Causal de Devolución],Descripción,[Fecha de rechazo],Cliente,Serie,Banco,Multa, [Fecha pago de multa], [Número de aviso])
AS 
   SELECT DISTINCT D.NROCHEQUE, D.SUCURSAL, D.CUENTA, VS.NOMBRE_CUENTA, D.PRODUCTO, D.MONEDA, D.IMPORTE, D.CONCDEVOLUCION, T.DESCRIPCION, B.FECHA_CHEQUE, D.CLIENTE, D.SERIE, D.CODBANCO, T.MULTA,B.FECHA_PAGO_MULTA, B.Nro_Aviso
   FROM CLE_CHEQUES_CLEARING_DEVUELTOS D 
   INNER JOIN CHE_BCO_RECHAZADOS B ON B.SERIE_CHEQUE = D.SERIE AND B.NRO_CHEQUE=D.NROCHEQUE AND 
   B.SUCURSAL = D.SUCURSAL AND B.MONEDA = D.MONEDA AND B.PRODUCTO=D.PRODUCTO AND B.CUENTA = D.CUENTA
   INNER JOIN CLE_TIPO_CAUSAL T ON D.CONCDEVOLUCION = T.CODIGO_DE_CAUSAL 
   INNER JOIN SALDOS S ON D.SUCURSAL = S.SUCURSAL AND D.MONEDA = S.MONEDA AND D.PRODUCTO = S.PRODUCTO AND D.CUENTA = S.CUENTA
   INNER JOIN VTA_SALDOS VS ON S.JTS_OID = VS.JTS_OID_SALDO
')
EXECUTE('
IF OBJECT_ID (''dbo.VW_TJD_COBRO_COMISIONES'') IS NOT NULL
	DROP VIEW dbo.VW_TJD_COBRO_COMISIONES

')
EXECUTE('
CREATE   VIEW [dbo].[VW_TJD_COBRO_COMISIONES] 
AS
SELECT 
	CAST (com.ID_SOLICITUD AS NUMERIC (12)) AS ID_SOLICITUD,
	CAST (com.SALDO_JTS_OID AS NUMERIC (12)) AS SALDO_JTS_OID,
	CAST (ROW_NUMBER() OVER(ORDER BY (SELECT 1)) AS NUMERIC (12)) AS ID_REG
FROM 
	TJD_COBRO_COMISIONES AS com WITH(nolock)
		
WHERE com.TZ_LOCK=0
')

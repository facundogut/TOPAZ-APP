EXECUTE('IF OBJECT_ID (''VW_CLIENTES_PAQUETES_VISTA'') IS NOT NULL
	DROP VIEW VW_CLIENTES_PAQUETES_VISTA')

EXECUTE('
CREATE     VIEW [VW_CLIENTES_PAQUETES_VISTA] (
													CLIENTEPR, 
													PRODUCTOCL, 
													MONEDAPR, 
													GRUPO, 
													PAQUETEPQ, 
													ACTIVOPQ)
AS 
SELECT TOP 9223372036854775807 WITH TIES 
		V.COD_CLIENTE AS CLIENTEPR, 
		P.PRODUCTO AS PRODUCTOCL, 
		P.MONEDA AS MONEDAPR, 
		P.GRUPO, 
		V.COD_PAQUETE AS PAQUETEPQ, 
		V.ACTIVO AS ACTIVOPQ
FROM CLI_CLIENTES_PAQUETES  AS V WITH(NOLOCK)
INNER JOIN CLI_PAQUETE_PRODUCTOS  AS P WITH(NOLOCK)ON V.COD_PAQUETE = P.COD_PAQUETE 
														AND P.ACTIVO = 1 
														AND V.ACTIVO = 1 
														AND (	(V.TZ_LOCK < 300000000000000 OR V.TZ_LOCK >= 400000000000000) 
															AND (V.TZ_LOCK < 100000000000000 OR V.TZ_LOCK >= 200000000000000)) 
														AND(	(P.TZ_LOCK < 300000000000000 OR P.TZ_LOCK >= 400000000000000) 
															AND (P.TZ_LOCK < 100000000000000 OR P.TZ_LOCK >= 200000000000000))
ORDER BY CLIENTEPR')






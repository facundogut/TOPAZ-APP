EXECUTE('
create VIEW VW_SOLICITUD_BAJA_ASIENTO AS
SELECT 
s.ASIENTO AS ''Asiento'',
a.DESCRIPCION AS ''Descripción Asiento'',
s.SUCURSAL AS ''Sucursal'',
s.FECHAPROCESO AS ''Fecha'',
a.OPERACION AS ''Operación'',
o.NOMBRE AS ''Nombre Operación'',
s.ESTADO AS ''Estado'',
s.USUARIO AS ''Usuario''
FROM CAJ_SOLICITUD_BAJA_ASIENTO s LEFT JOIN ASIENTOS a ON s.ASIENTO=a.ASIENTO AND s.SUCURSAL=a.SUCURSAL AND s.FECHAPROCESO=a.FECHAPROCESO
LEFT JOIN OPERACIONES o ON a.OPERACION=o.IDENTIFICACION
WHERE ((s.TZ_LOCK < 300000000000000 OR s.TZ_LOCK >= 400000000000000) AND (s.TZ_LOCK < 100000000000000 OR s.TZ_LOCK >= 200000000000000))
')
--VW_CBU_VTA_SALDOS

EXECUTE('IF OBJECT_ID (''VW_CBU_VTA_SALDOS'') IS NOT NULL
	DROP VIEW dbo.VW_CBU_VTA_SALDOS
')
EXECUTE('
CREATE VIEW VW_CBU_VTA_SALDOS
AS
SELECT  VS.CTA_CBU AS ''Cuenta CBU'', 
		VS.NOMBRE_CUENTA AS ''Nombre Cuenta'' 
FROM VTA_SALDOS VS WITH(NOLOCK)
WHERE (((TZ_LOCK >= 100000000000000 AND TZ_LOCK < 300000000000000) OR TZ_LOCK >= 400000000000000) OR TZ_LOCK = 0)
')
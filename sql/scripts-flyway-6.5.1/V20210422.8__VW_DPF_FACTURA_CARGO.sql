CREATE VIEW [dbo].[VW_DPF_FACTURA_CARGO]
										(
										JTS_OID,
										MONTO,
										[CARGO]
										)
AS
			SELECT	F.JTS_SALDO_CLIENTE AS JTS_OID,
					F.IMPORTEMN AS MONTO,
					L.[ID_CARGO] AS [CARGO] 
			FROM	CI_FACTURA F WITH (NOLOCK)
					INNER JOIN CI_FACTURA_LINEA L WITH (NOLOCK) ON L.ASIENTO = F.ASIENTO
															AND L.FECHA_PROCESO_ASIENTO = F.FECHA_PROCESO_ASIENTO
															AND F.SUCURSAL_ASIENTO = L.SUCURSAL_ASIENTO 

GO



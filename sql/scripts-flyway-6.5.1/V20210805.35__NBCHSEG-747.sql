c.CONC_ACREEDORES,
UPPER(c.OBSERVACIONES),
(CASE 
WHEN d.TIPOPERSONA=''F'' THEN ''PERSONA HUMANA''
WHEN d.TIPOPERSONA=''J'' THEN ''PERSONA JURÍDICA''
WHEN d.TIPOPERSONA=''I'' THEN ''PERSONA INSTITUCÓN FINANCIERA''
END) AS ''Tipo Persona'',
(CASE 
WHEN d.TIPOPERSONA=''F'' THEN (SELECT DESCRIPCION FROM OPCIONES WITH (NOLOCK) WHERE NUMERODECAMPO=2216 AND OPCIONINTERNA=p.NIVEL_APERTURA AND IDIOMA=''E'')
WHEN d.TIPOPERSONA=''J'' THEN (SELECT DESCRIPCION FROM OPCIONES WITH (NOLOCK) WHERE NUMERODECAMPO=2216 AND OPCIONINTERNA=u.NIVEL_APERTURA AND IDIOMA=''E'')

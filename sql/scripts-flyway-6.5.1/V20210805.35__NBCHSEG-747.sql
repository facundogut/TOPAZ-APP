CREATE VIEW VW_CONCURSO_ACREEDORES 
(
[T. Doc. Ident.],
[Nro. Doc. Ident.],
[Ape./s y Nomb./s - Razón Social],
[Juzgado],
[Desc. Juzgado],
[Año],
[Expediente],

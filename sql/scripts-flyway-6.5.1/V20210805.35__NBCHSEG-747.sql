END) AS ''Motivo'',
c.ID
FROM CLI_CONCURSO_ACREEDORES c  WITH (NOLOCK)
JOIN CLI_DocumentosPFPJ d WITH (NOLOCK) ON c.CUIT_CUIL=d.NUMERODOCUMENTO AND c.TIPODOCUMENTO=d.TIPODOCUMENTO AND
((d.TZ_LOCK < 300000000000000 OR d.TZ_LOCK >= 400000000000000) AND (d.TZ_LOCK < 100000000000000 OR d.TZ_LOCK >= 200000000000000)) AND
((c.TZ_LOCK < 300000000000000 OR c.TZ_LOCK >= 400000000000000) AND (c.TZ_LOCK < 100000000000000 OR c.TZ_LOCK >= 200000000000000))
LEFT JOIN DJ_JUZGADOS j WITH (NOLOCK) ON c.JUZGADO=j.NRO_JUZGADO AND
((j.TZ_LOCK < 300000000000000 OR j.TZ_LOCK >= 400000000000000) AND (j.TZ_LOCK < 100000000000000 OR j.TZ_LOCK >= 200000000000000))
LEFT JOIN CLI_PERSONASFISICAS p WITH (NOLOCK) ON d.TIPOPERSONA=''F'' AND p.NUMEROPERSONAFISICA=d.NUMEROPERSONAFJ LEFT JOIN CLI_PERSONASJURIDICAS u WITH (NOLOCK) ON d.TIPOPERSONA=''J'' AND u.NUMEROPERSONAJURIDICA=d.NUMEROPERSONAFJ

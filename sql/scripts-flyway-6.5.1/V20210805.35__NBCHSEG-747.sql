SELECT 
c.TIPODOCUMENTO,
c.CUIT_CUIL,
(CASE 
WHEN d.TIPOPERSONA=''F'' THEN p.APELLIDOPATERNO+'' ''+p.PRIMERNOMBRE 
ELSE u.RAZONSOCIAL 

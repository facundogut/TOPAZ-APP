EXECUTE ('IF OBJECT_ID (''VW_USUARIOS'') IS NOT NULL
	DROP VIEW VW_USUARIOS')

EXECUTE('
CREATE     VIEW [VW_USUARIOS] (
										   NOMBRE, 
										   CLAVE, 
										   INICIALES, 
										   GRUPO, 
										   NROSUCURSAL)
AS 
SELECT TOP 9223372036854775807 WITH TIES 
    USUARIOS.NOMBRE, 
    USUARIOS.CLAVE, 
    USUARIOS.INICIALES, 
    USUARIOS.GRUPO, 
    USUARIOS.NROSUCURSAL
FROM dbo.USUARIOS WITH(NOLOCK)
ORDER BY USUARIOS.NOMBRE')



EXECUTE('
IF OBJECT_ID (''dbo.VW_CONV_DOMINIOS_EXCAJA'') IS NOT NULL
	DROP VIEW dbo.VW_CONV_DOMINIOS_EXCAJA
')

EXECUTE('
CREATE VIEW VW_CONV_DOMINIOS_EXCAJA 
AS
SELECT ID_CONVENIO AS Convenio, ID_DOMINIO AS Dominio, DESCRIPCION AS Descripcion
	FROM CONV_DOMINIOS WITH(NOLOCK)
	WHERE TZ_LOCK = 0 AND TIPO_DOMINIO=''E'' 
	GROUP BY ID_CONVENIO, ID_DOMINIO, DESCRIPCION
')
EXECUTE ('
CREATE OR ALTER VIEW dbo.VW_PH_DOCUMENTOS_HAB ( 
										TIPODOCIDENT,
					   					NUMERODOCIDENT,
					   					TIPO_DOC_FISICO,
					  					NUM_DOC_FISICO,
					   					APELLIDONOMBRE,
					  					TIPOPERSONA,
					  					NIVEL_APERTURA,
					  					ESTADO,
					 					MOTIVO_INHABILITADO,
					  					NUMEROPERSONAFJ,
					  					TZ_LOCK
					  				   )
AS
(SELECT
	V.TIPODOCUMENTO , 
    V.NUMERODOCUMENTO ,
    V.TIPO_DOC_FISICO ,
    V.NUM_DOC_FISICO ,
    (U.APELLIDOPATERNO +'' ''+U.PRIMERNOMBRE) AS NOMBRES,
    V.TIPOPERSONA ,  
    (	SELECT DESCRIPCION FROM OPCIONES O WITH(NOLOCK)
			WHERE O.IDIOMA=''E'' AND O.OPCIONINTERNA=U.NIVEL_APERTURA AND O.NUMERODECAMPO = 2203) AS NIVEL_APERTURA,
    (	SELECT DESCRIPCION FROM OPCIONES O WITH(NOLOCK)
			WHERE O.IDIOMA=''E'' AND O.OPCIONINTERNA=U.ESTADO AND O.NUMERODECAMPO = 33366) AS ESTADO,   
	(	SELECT DESCRIPCION FROM OPCIONES O  WITH(NOLOCK)
			WHERE O.IDIOMA=''E'' AND O.OPCIONINTERNA=U.MOTIVO_INHABILITADO AND O.NUMERODECAMPO = 33368) AS  MOTIVO_INHABILITADO,
    V.NUMEROPERSONAFJ,
    u.TZ_LOCK 
       
FROM dbo.CLI_PERSONASFISICAS  AS U WITH(NOLOCK)
INNER JOIN dbo.CLI_DOCUMENTOSPFPJ  AS V WITH(NOLOCK)ON 
													U.NUMEROPERSONAFISICA = V.NUMEROPERSONAFJ 
													AND V.TIPOPERSONA = ''F''
													AND U.TZ_LOCK =0
													AND V.TZ_LOCK =0
)

')
EXECUTE('
IF OBJECT_ID (''dbo.VW_ORDEN_COBRANZA_ASISTENCIAS'') IS NOT NULL
	DROP VIEW dbo.VW_ORDEN_COBRANZA_ASISTENCIAS
')

EXECUTE('
CREATE VIEW dbo.VW_ORDEN_COBRANZA_ASISTENCIAS
AS 
SELECT cod.CODIGO,b.ORDEN,b.tipo, b.codigo_item
FROM CRE_COB_CODIGOS cod with (nolock)
INNER JOIN CRE_COB_ORDEN_COBRANZA b with (nolock) ON cod.CODIGO=b.CODIGO_COBRANZA AND b.TZ_LOCK=0
WHERE cod.SOLO_ACREDITA=''N'' AND cod.TZ_LOCK=0
')
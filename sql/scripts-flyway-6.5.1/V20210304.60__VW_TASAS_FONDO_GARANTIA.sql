CREATE VIEW [dbo].[VW_TASAS_FONDO_GARANTIA]
					(	TIPO_PRODUCTO,
						DESCRIPCION_TIPO_PRODUCTO,
						MONTO_MAXIMO,PLAZO,
						FECHA_VIGENCIA,
						TASA_MN,
						TASA_DOLARES,
						TASA_UVA,
						TASA_UVI
					)
AS
SELECT	T.TIPOPRODUCTO, 
		O.DESCRIPCION, 
		T.MONTO_MAX, 
		T.PLAZO, 
		T.FECHA_VIGENCIA, 
		T.TASA_MN, 
		T.TASA_DOL, 
		T.TASA_UVA, 
		T.TASA_UVI
FROM	TASASFONDOGARANTIA AS T with (nolock)
		inner join OPCIONES AS O with (nolock) on O.OPCIONINTERNA = T.TIPOPRODUCTO
WHERE O.NUMERODECAMPO = 33485 
		AND O.IDIOMA = 'E'  
		AND T.TZ_LOCK = 0
GO



CREATE VIEW [dbo].[VW_REPORTES_CTA_VISTA] (
   NUMERO,
   DESCRIPCION
   )
AS 
   SELECT
      O.OPCIONINTERNA AS NUMERO,
      O.DESCRIPCION
   FROM 
      dbo.OPCIONES AS O with (nolock)
   WHERE 
      O.NUMERODECAMPO = 34260 
	  AND O.IDIOMA = 'E'
GO



EXECUTE('
ALTER VIEW [dbo].[VW_TJD_CANAL_ORIGEN] (
							CANAL,
							DESCRIPCION_CANAL,
							ORIGEN,
							DESCRIPCION_ORIGEN,
							DESCRIPCION_RELACION
							)
AS
				SELECT CO.CANAL, 
						TC.DESCRIPCION, 
						CO.ORIGEN,
						TJO.DESCRIPCION, 
						CO.DESCRIPCION AS DESCRIPCION_RELACION
				FROM TJD_CANAL_ORIGEN CO WITH (nolock)
				INNER JOIN TJD_CANAL TC WITH (nolock) ON CO.CANAL = TC.CODIGO 
				INNER JOIN TJD_ORIGEN TJO WITH (nolock) ON CO.ORIGEN = TJO.CODIGO 
						AND CO.TZ_LOCK = 0 
						AND TC.TZ_LOCK = 0
						AND TJO.TZ_LOCK = 0

')
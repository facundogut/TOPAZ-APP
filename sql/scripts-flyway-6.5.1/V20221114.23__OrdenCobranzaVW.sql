EXECUTE('
IF OBJECT_ID (''dbo.VW_ORDEN_COBRANZA'') IS NOT NULL
	DROP VIEW dbo.VW_ORDEN_COBRANZA
')
EXECUTE('
CREATE VIEW dbo.VW_ORDEN_COBRANZA AS (
SELECT TOP 10000 CO.ORDEN,CO.CODIGO_COBRANZA,
CC.DESCRIPCION,CO.CORRELATIVO,
CO.TIPO,O.DESCRIPCION AS ''DESCRIPCIÓN TIPO'',CO.CODIGO_ITEM,  
CASE WHEN CO.TIPO=''F'' THEN FAM.DESCRIPCION    
     WHEN CO.TIPO=''C'' THEN CCR.NomConvRec
     WHEN CO.TIPO=''P'' THEN P.C6251
    ELSE ''sin desc'' END AS ''DESCRIPCIÓN ITEM''
FROM CRE_COB_CODIGOS AS CC WITH(NOLOCK)
INNER JOIN CRE_COB_ORDEN_COBRANZA AS CO WITH(NOLOCK)
ON CC.CODIGO=CO.CODIGO_COBRANZA
LEFT JOIN CONV_CONVENIOS_REC AS CCR WITH(NOLOCK) ON CO.CODIGO_ITEM=CCR.Id_ConvRec
LEFT JOIN PRODUCTOS AS P WITH(NOLOCK) ON CO.CODIGO_ITEM=P.C6250
LEFT JOIN CRE_FAMILIAS AS FAM WITH(NOLOCK) ON CO.CODIGO_ITEM=FAM.FAMILIAPROD
INNER JOIN OPCIONES AS O WITH(NOLOCK) ON O.NUMERODECAMPO=45382 AND O.OPCIONINTERNA=CO.TIPO
AND O.IDIOMA=''E'' 
WHERE CO.TZ_LOCK=0
ORDER BY CO.ORDEN 
)
')


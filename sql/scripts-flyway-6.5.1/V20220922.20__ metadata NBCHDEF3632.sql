	   CR.FECHA AS ''Fecha'',
	   CR.NUMERO_SERIE AS ''NumeroSerie'',
	   CR.ESTADO_DEVOLUCION  
FROM CLE_CHEQUES_CLEARING_RECIBIDO CR WITH(NOLOCK)
INNER JOIN SALDOS S WITH(NOLOCK) ON CR.SUCURSAL = S.SUCURSAL 
											AND CR.MONEDA = S.MONEDA 
											AND CR.CUENTA = S.CUENTA 
											AND CR.PRODUCTO = S.PRODUCTO 
											AND CR.OPERACION = S.OPERACION 
											AND S.ORDINAL = 0 
											AND (	(CR.TZ_LOCK < 300000000000000 OR CR.TZ_LOCK >= 400000000000000) 
												AND (CR.TZ_LOCK < 100000000000000 OR CR.TZ_LOCK >= 200000000000000)) 
											AND(	(S.TZ_LOCK < 300000000000000 OR S.TZ_LOCK >= 400000000000000) 
												AND (S.TZ_LOCK < 100000000000000 OR S.TZ_LOCK >= 200000000000000))
INNER JOIN VTA_SALDOS VS WITH(NOLOCK) ON S.JTS_OID = VS.JTS_OID_SALDO

	   MONEDA, 
	   CUENTA,
	   NOMBRE_CUENTA,
	   PRODUCTO,
	   NUMERO_CHEQUE,
	   IMPORTE_CHEQUE,
	   CODIGO_CAUSAL_DEVOLUCION,
	   FECHA,
	   NUMERO_SERIE,
	   ESTADO_DEVOLUCION )
AS
SELECT CR.SUCURSAL AS ''Sucursal'', 
	   CR.MONEDA AS ''Moneda'', 

	SELECT TOP 9223372036854775807 WITH TIES 
      V.TIPODOCUMENTO , 
      V.NUMERODOCUMENTO,
      V.TIPO_DOC_FISICO,
      V.NUM_DOC_FISICO,
      concat(U.APELLIDOPATERNO,'' '', U.APELLIDOMATERNO,'' '', U.PRIMERNOMBRE,'' '', U.SEGUNDONOMBRE) AS NOMBRE,

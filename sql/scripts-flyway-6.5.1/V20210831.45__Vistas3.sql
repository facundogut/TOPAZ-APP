Execute('
create VIEW VW_CAJ_CAJAS_MINIFILIALES as
SELECT 
	cm.Mini_Filial AS Minifilial, 
	cm.NRO_CAJA AS ''Número de Caja'',
	(SELECT DESCRIPCION FROM OPCIONES WHERE NUMERODECAMPO = 5586 AND OPCIONINTERNA = cm.EsTesoro) as ''Es Tesoro''
from CAJ_Cajas_Minifiliales cm
where cm.TZ_LOCK = 0
')
Execute('
CREATE VIEW VW_REM_MINIFILIALES 
AS
SELECT rs.NRO_SOLICITUD ''Nro. Solicitud'', rs.MONEDA Moneda, rs.IMPORTE Importe, rs.TIPODESTINO ''Tipo Destino'', o.DESCRIPCION ''Descripción'', rs.CAJA_DESTINO ''Caja Destino'', vwc.MINIFILIAL ''Minifilial Destino'', rs.SUCURSAL Sucursal, rs.ESTADO Estado, rs.CAJA_ORIGEN ''Caja Origen'' FROM REM_SOLICITUDREMESA rs, OPCIONES o, VW_CAJ_CAJAS vwc
WHERE o.OPCIONINTERNA = rs.TIPODESTINO
AND o.NUMERODECAMPO = 2599
AND o.IDIOMA = ''E''
AND vwc.SUCURSAL = CAST(rs.DESTINO AS int) 
AND vwc.NRO_CAJA = rs.CAJA_DESTINO
AND vwc.MINIFILIAL > 0
AND rs.TIPODESTINO = 11
AND rs.TIPOORIGEN = 11
')
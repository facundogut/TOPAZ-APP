
EXECUTE('

ALTER VIEW VW_PYF_TEXTOS AS
SELECT a.ID_ENTIDAD AS IdEntidad,a.TIPO_ENTIDAD AS TipoEntidad,a.TIPO_PODER AS TipoPoder, PT.DESCRIPCION AS DescripcionPoder,
CASE WHEN a.TIPO_ENTIDAD=2 THEN
ISNULL((SELECT TIPODOC FROM VW_CLI_X_DOC WHERE CODIGOCLIENTE = s.C1803),0) 
ELSE
ISNULL((SELECT TIPODOC FROM VW_CLI_X_DOC WHERE CODIGOCLIENTE = CAST(a.ID_ENTIDAD AS NUMERIC)),0) end
AS TipoDoc,
CASE WHEN a.TIPO_ENTIDAD=2 THEN
ISNULL((SELECT NUMERODOC FROM VW_CLI_X_DOC WHERE CODIGOCLIENTE = s.C1803),0) 
ELSE
ISNULL((SELECT NUMERODOC FROM VW_CLI_X_DOC WHERE CODIGOCLIENTE = CAST(a.ID_ENTIDAD AS NUMERIC)),0) end
AS NumDoc,
isnull(s.CUENTA,0) AS Cuenta
 FROM PYF_TEXTOS a
LEFT JOIN SALDOS s ON s.JTS_OID=a.ID_ENTIDAD AND a.TIPO_ENTIDAD=2 AND s.TZ_LOCK=0
INNER JOIN PYF_TIPOPODERES PT ON a.TIPO_PODER = PT.TIPO_PODER 
WHERE a.TZ_LOCK=0;

')
execute ('

UPDATE AYUDAS
SET CAMPOS=''5010R;2631R;3100R;114;3249;3843;3796;'', CAMPOSVISTA=''IdEntidad;TipoEntidad;TipoPoder;DescripcionPoder;TipoDoc;NumDoc;Cuenta;''
WHERE NUMERODEAYUDA=3402;

')
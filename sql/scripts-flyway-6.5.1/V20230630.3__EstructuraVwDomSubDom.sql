EXECUTE('
IF OBJECT_ID (''dbo.VW_DOMINIOS'') IS NOT NULL
	DROP VIEW dbo.VW_DOMINIOS
')
EXECUTE('
CREATE   VIEW [dbo].[VW_DOMINIOS] 
AS
SELECT  conv.ID_DOMINIO, MAX(conv.DESCRIPCION) AS DESCRIPCION, conv.TIPO_DOMINIO
FROM CONV_DOMINIOS conv WITH(NOLOCK)
GROUP BY  conv.ID_DOMINIO, conv.TIPO_DOMINIO
')
EXECUTE('
IF OBJECT_ID (''dbo.VW_SUBDOMINIOS'') IS NOT NULL
	DROP VIEW dbo.VW_SUBDOMINIOS
')
EXECUTE('
CREATE   VIEW [dbo].[VW_SUBDOMINIOS] 
AS
SELECT conv.ID_DOMINIO, conv.SUB_DOMINIO AS ID_SUBDOMINIO, MAX(conv.DESC_SUB_DOMINIO) AS DESCRIPCION, conv.TIPO_DOMINIO
FROM CONV_DOMINIOS conv WITH(NOLOCK)
GROUP BY conv.ID_DOMINIO, conv.SUB_DOMINIO, conv.TIPO_DOMINIO
')
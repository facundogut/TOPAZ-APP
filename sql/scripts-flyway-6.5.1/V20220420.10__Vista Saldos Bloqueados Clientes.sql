EXECUTE('
IF OBJECT_ID (''VW_BLOQUEOS_SALDOS_CLIENTES'') IS NOT NULL
	DROP VIEW VW_BLOQUEOS_SALDOS_CLIENTES
')


EXECUTE('
CREATE VIEW VW_BLOQUEOS_SALDOS_CLIENTES AS

SELECT 
		cod.COD_BLOQUEO,
		BL.SALDO_JTS_OID,
		BL.ORDINAL_BLOQUEO,
		s.C1803 AS CLIENTE
	  
FROM
		GRL_BLOQUEOS bl with (nolock)
		INNER JOIN GRL_COD_BLOQUEOS cod with (nolock)
                  ON bl.COD_BLOQUEO=cod.COD_BLOQUEO AND cod.TZ_LOCK=0
        INNER JOIN SALDOS s WITH (nolock)
        		  ON s.JTS_OID = bl.SALDO_JTS_OID AND s.TZ_LOCK=0


WHERE	bl.TZ_LOCK=0 AND
		bl.ESTADO=1 
')		


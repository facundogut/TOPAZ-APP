EXECUTE('

ALTER   VIEW [dbo].[VW_CONCURSO_ACREEDORES](
													[T. Doc. Ident.],
													[Nro. Doc. Ident.],
													[Ape./s y Nomb./s - Razón Social],
													[Juzgado],
													[Desc. Juzgado],
													[Año],
													[Expediente],
													[Carátula],
													[F. Ingreso],
													[F. Fin],
													[Medio],
													[Desc. Medio],
													[Concurso Acreedores],
													[Observaciones],
													[Tipo persona],
													[Nivel Aper.],
													[Estado Persona],
													[Motivo Inhabilitado],
													[Núm. CA])
AS

SELECT 
	c.TIPODOCUMENTO,
	c.CUIT_CUIL,
	ISNULL((CASE 
	WHEN d.TIPOPERSONA=''F'' THEN p.APELLIDOPATERNO+'' ''+p.PRIMERNOMBRE 
	ELSE u.RAZONSOCIAL 
	END),(SELECT NOMBRE_COMPLETO FROM ITF_BCRA_PADFYJ WHERE CUIT=c.CUIT_CUIL AND TZ_LOCK=0)) AS ''Nombre o Razón'',
	c.JUZGADO,
	UPPER(j.NOMBRE),
	c.ANIO,
	c.EXPEDIENTE,
	UPPER(c.CARATULA),
	c.FECHA_INGRESO,
	c.FECHA_FIN,
	UPPER(c.MEDIO),
		(SELECT DESCRIPCION 
		FROM OPCIONES WITH (NOLOCK) 
		WHERE NUMERODECAMPO=33031 
		AND OPCIONINTERNA=c.MEDIO 
		AND IDIOMA=''E'') AS ''Descripcion medio'',
	c.CONC_ACREEDORES,
	UPPER(c.OBSERVACIONES),
	(CASE 
		WHEN d.TIPOPERSONA=''F'' THEN ''PERSONA HUMANA''
		WHEN d.TIPOPERSONA=''J'' THEN ''PERSONA JURÍDICA''
		WHEN d.TIPOPERSONA=''I'' THEN ''PERSONA INSTITUCÓN FINANCIERA''
	END) AS ''Tipo Persona'',
	(CASE 
		WHEN d.TIPOPERSONA=''F'' THEN (SELECT DESCRIPCION FROM OPCIONES WITH (NOLOCK) WHERE NUMERODECAMPO=2216 AND OPCIONINTERNA=p.NIVEL_APERTURA AND IDIOMA=''E'')
		WHEN d.TIPOPERSONA=''J'' THEN (SELECT DESCRIPCION FROM OPCIONES WITH (NOLOCK) WHERE NUMERODECAMPO=2216 AND OPCIONINTERNA=u.NIVEL_APERTURA AND IDIOMA=''E'')
	END) AS ''Nivel apertura'',
	(CASE 
		WHEN d.TIPOPERSONA=''F'' THEN UPPER((SELECT DESCRIPCION FROM OPCIONES WITH (NOLOCK) WHERE NUMERODECAMPO=33366 AND IDIOMA=''E'' AND OPCIONINTERNA=p.ESTADO))
		WHEN d.TIPOPERSONA=''J'' THEN UPPER((SELECT DESCRIPCION FROM OPCIONES WITH (NOLOCK) WHERE NUMERODECAMPO=33367 AND IDIOMA=''E'' AND OPCIONINTERNA=u.ESTADO))
	END) AS ''Estado'',
	(CASE 
		WHEN d.TIPOPERSONA=''F'' THEN UPPER((SELECT DESCRIPCION FROM OPCIONES WITH (NOLOCK) WHERE NUMERODECAMPO=33368 AND IDIOMA=''E'' AND OPCIONINTERNA=p.MOTIVO_INHABILITADO))
		WHEN d.TIPOPERSONA=''J'' THEN UPPER((SELECT DESCRIPCION FROM OPCIONES WITH (NOLOCK) WHERE NUMERODECAMPO=33369 AND IDIOMA=''E'' AND OPCIONINTERNA=u.MOTIVO_INHABILITADO))
	END) AS ''Motivo'',
	c.ID
FROM CLI_CONCURSO_ACREEDORES c  WITH (NOLOCK)
LEFT JOIN CLI_DocumentosPFPJ d WITH (NOLOCK) ON c.CUIT_CUIL=d.NUMERODOCUMENTO 
												AND c.TIPODOCUMENTO=d.TIPODOCUMENTO 
												AND(	(d.TZ_LOCK < 300000000000000 OR d.TZ_LOCK >= 400000000000000) 
													AND (d.TZ_LOCK < 100000000000000 OR d.TZ_LOCK >= 200000000000000)) 
												AND(	(c.TZ_LOCK < 300000000000000 OR c.TZ_LOCK >= 400000000000000) 
													AND (c.TZ_LOCK < 100000000000000 OR c.TZ_LOCK >= 200000000000000))
LEFT JOIN DJ_JUZGADOS j WITH (NOLOCK) ON c.JUZGADO=j.NRO_JUZGADO 
										AND(	(j.TZ_LOCK < 300000000000000 OR j.TZ_LOCK >= 400000000000000) 
											AND (j.TZ_LOCK < 100000000000000 OR j.TZ_LOCK >= 200000000000000))
LEFT JOIN CLI_PERSONASFISICAS p WITH (NOLOCK) ON d.TIPOPERSONA=''F'' 
												AND p.NUMEROPERSONAFISICA=d.NUMEROPERSONAFJ 
LEFT JOIN CLI_PERSONASJURIDICAS u WITH (NOLOCK) ON d.TIPOPERSONA=''J'' 
												AND u.NUMEROPERSONAJURIDICA=d.NUMEROPERSONAFJ




')
CREATE VIEW [dbo].[VW_CONTROLES_CIERRE] (
   CONTROL,
   DESCRIPCION,
   ESTADO
   )
AS 
   SELECT
      O.OPCIONINTERNA AS OPCION,
      O.DESCRIPCION,
	  'Sin realizar'
   FROM 
      dbo.OPCIONES AS O WITH (NOLOCK)
   WHERE 
      O.NUMERODECAMPO = 34549 
	  AND O.IDIOMA = 'E'

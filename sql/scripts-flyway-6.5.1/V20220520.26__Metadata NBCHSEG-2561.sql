SELECT L.ID_LIQUIDACION AS ''ID_LIQUIDACION'', 
		L.ESTADO AS ''ESTADO'', 
		L.CONVENIO AS ''CONVENIO'', 
		C.NomConvRec AS ''NOMBRE_CONVENIO'', 

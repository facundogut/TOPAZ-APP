EXECUTE('
DELETE CO_AJUSTE_X_INFLACION WHERE RUBRO_ORIGEN LIKE ''%70''

DECLARE @6326 NUMERIC(12);
DECLARE @monedaO VARCHAR(1);
DECLARE @moneda VARCHAR(1);

DECLARE db_cursor CURSOR FOR

SELECT b.C6326, b.C6302 FROM PLANCTAS b
WHERE b.TZ_LOCK = 0
AND b.C6326 LIKE ''%70''

OPEN db_cursor  
FETCH NEXT FROM db_cursor INTO @6326,@monedaO

--SELECT * FROM CO_AJUSTE_X_INFLACION
WHILE @@FETCH_STATUS = 0  
BEGIN  

IF  @monedaO = ''N''
  BEGIN 

  INSERT INTO CO_AJUSTE_X_INFLACION (RUBRO_ORIGEN, MONEDA, RUBRO_DESTINO, RUBRO_PATRIMONIAL, TIPO_INDICE, TZ_LOCK, MONETARIO)
      VALUES (@6326, ''N'', @6326, 3211510131, 1, 0, ''N'')

  END

  FETCH NEXT FROM db_cursor INTO @6326 ,@monedaO

END

CLOSE db_cursor  
DEALLOCATE db_cursor

UPDATE c
SET c.RUBRO_PATRIMONIAL=6200040010
FROM CO_AJUSTE_X_INFLACION c
INNER JOIN planctas p ON c.RUBRO_ORIGEN = p.C6326  
AND p.C6304 IN (8,9)--RESULTADO MONETARIO POR CTAS RESULTADOS --- resultados

UPDATE c
SET c.RUBRO_PATRIMONIAL=6200040000
FROM CO_AJUSTE_X_INFLACION c
INNER JOIN planctas p ON c.RUBRO_ORIGEN = p.C6326  AND p.C6304 IN (1,2,3)
---RESULTADO MONETARIO POR CTAS PATRIMONIALES  activo, pasivo y patrimonio
')
CREATE VIEW [dbo].[VW_DESBLOQUEOS] AS

SELECT 
		cod.COD_BLOQUEO AS [Código de Bloqueo],
		cod.DESCRIPCION AS [Descripción Bloqueo],
		bl.USUARIO_MODIFICACION AS [Usuario],
		bl.FECHA_MODIFICACION AS [Fecha desbloqueo],
		BL.DESCRIPCION AS [Descripción],
		o.DESCRIPCION AS [Por orden de],
		BL.SOLICITADOPOR,
		BL.SALDO_JTS_OID
FROM
		GRL_BLOQUEOS bl with (nolock)
		inner JOIN GRL_COD_BLOQUEOS cod with (nolock)
                  ON (bl.COD_BLOQUEO=cod.COD_BLOQUEO)
		LEFT JOIN OPCIONES o with (nolock)
                  ON (o.NUMERODECAMPO=25098 AND o.IDIOMA=''E'' AND bl.PORORDEN=o.OPCIONINTERNA)

WHERE	bl.TZ_LOCK=0 AND
		cod.TZ_LOCK=0 AND
		bl.ESTADO=2
')


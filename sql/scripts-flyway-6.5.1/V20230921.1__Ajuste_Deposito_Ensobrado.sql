EXECUTE('
IF OBJECT_ID (''dbo.VW_CUENTAS_DEPOSITO_ENSOBRADO'') IS NOT NULL
	DROP VIEW dbo.VW_CUENTAS_DEPOSITO_ENSOBRADO

')

EXECUTE('
CREATE VIEW VW_CUENTAS_DEPOSITO_ENSOBRADO AS
SELECT O.DESCRIPCION AS ''TIPO DEPOSITO'', T.Id AS ''ID'',T.TZ_LOCK,T.JTS_OID_CV ''JTS OID'',T.IMPORTE,T.ESTADO,T.FECHA,
C.CUENTA,C.NOMBRE, C.PRODUCTO,C.DESCRIPCION,C.MONEDA,C.DESCRIPCION_MONEDA AS ''DESC MONEDA'',
C.SUCURSAL,C.CLIENTE,C.TIPO_DOCUMENTO AS ''TIPO'',C.DOCUMENTO, T.MONEDA_DEPOSITO AS ''MONEDA DEPOSITO'',M.C6400 AS ''DESC MONEDA DEPOSITO''
FROM VTA_TRANSACCIONES_DIFERIDAS AS T WITH (nolock)
INNER JOIN VW_CUENTAS AS C WITH (nolock) 
ON T.JTS_OID_CV=C.JTS_OID
INNER JOIN MONEDAS AS M WITH (nolock) 
ON T.MONEDA_DEPOSITO=M.C6399
INNER JOIN OPCIONES AS O WITH(NOLOCK)
ON O.IDIOMA = ''E''AND O.NUMERODECAMPO = 35701 AND O.OPCIONINTERNA = T.TIPODEPOSITO 
WHERE T.TZ_LOCK=0 AND T.TIPODEPOSITO!=''C''

')
EXECUTE('
ALTER PROCEDURE SP_DJ_GENERAR_NRO_CTA
@pNroCliente NUMERIC (12) OUT, @pNroCta NUMERIC (12) OUT
 AS
BEGIN
	DECLARE
	@vCliente NUMERIC (12),
	@vNroCta NUMERIC (12)
	
	SELECT @vCliente = NUMERICO FROM PARAMETROSGENERALES WHERE CODIGO = 9
	
	SELECT @vNroCta = COALESCE(MAX(CUENTA), @vCliente * 100) + 1
	FROM SALDOS S 
	WHERE S.C1803=@vCliente --AND S.TZ_LOCK=0  POR NBCHSEG-551
	
	SET @pNroCliente = @vCliente;
	SET @pNroCta = @vNroCta
END
')


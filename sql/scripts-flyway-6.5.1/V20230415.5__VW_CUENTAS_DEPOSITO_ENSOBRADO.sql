FROM VTA_TRANSACCIONES_DIFERIDAS AS T WITH (nolock)
INNER JOIN VW_CUENTAS AS C WITH (nolock) 
ON T.JTS_OID_CV=C.JTS_OID
INNER JOIN MONEDAS AS M WITH (nolock) 
ON T.MONEDA_DEPOSITO=M.C6399
WHERE T.TZ_LOCK=0 


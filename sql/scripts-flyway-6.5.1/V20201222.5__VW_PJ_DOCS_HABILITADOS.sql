EXECUTE('

IF object_id(''VW_PJ_DOCS_HABILITADOS'') IS NOT  NULL 
DROP VIEW VW_PJ_DOCS_HABILITADOS
')
execute ('
CREATE  VIEW dbo.VW_PJ_DOCS_HABILITADOS (
   [Tipo Documento Identificativo], 
   [Número Documento Identificativo],
   [Razón Social],
   [Nivel de Apertura],
   [Estado de la Persona],
   [Número de Persona],
   [Tipo de Persona])
AS  
   SELECT TOP 9223372036854775807 WITH TIES 
      V.TIPODOCUMENTO , 
      V.NUMERODOCUMENTO ,
      (U.RAZONSOCIAL) AS RAZONSOCIAL,
      (SELECT DESCRIPCION FROM OPCIONES O WHERE O.IDIOMA=''E'' AND O.OPCIONINTERNA=U.NIVEL_APERTURA AND O.NUMERODECAMPO = 2203) AS NIVEL_APERTURA,
      (SELECT DESCRIPCION FROM OPCIONES O WHERE O.IDIOMA=''E'' AND O.OPCIONINTERNA=U.ESTADO AND O.NUMERODECAMPO = 33367) AS ESTADO,   
	  V.NUMEROPERSONAFJ,
	   V.TIPOPERSONA 
       
   FROM dbo.CLI_PERSONASJURIDICAS  AS U, dbo.CLI_DOCUMENTOSPFPJ  AS V
   WHERE 
      U.NUMEROPERSONAJURIDICA = V.NUMEROPERSONAFJ AND 
      (V.TIPOPERSONA = ''J'') AND 
      (U.TZ_LOCK =0) AND 
      (V.TZ_LOCK =0)
   ORDER BY NUMEROPERSONAFJ
')
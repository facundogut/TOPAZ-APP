
EXECUTE('
IF OBJECT_ID (''dbo.VW_CONV_DOMINIOS'') IS NOT NULL
	DROP VIEW dbo.VW_CONV_DOMINIOS
')

EXECUTE('
CREATE VIEW dbo.VW_CONV_DOMINIOS AS
	SELECT ID_CONVENIO, ID_DOMINIO, DESCRIPCION,  TIPO_DOMINIO, DESC_SUB_DOMINIO, TIPO_SUB_DOMINIO, SUB_DOMINIO
	FROM CONV_DOMINIOS WITH(NOLOCK)
	WHERE TZ_LOCK = 0 AND TIPO_DOMINIO=''J''
')



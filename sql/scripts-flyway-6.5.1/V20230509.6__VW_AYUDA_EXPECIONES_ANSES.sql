EXECUTE('
IF OBJECT_ID (''dbo.VW_AYUDA_EXPECIONES_ANSES'') IS NOT NULL
	DROP VIEW dbo.VW_AYUDA_EXPECIONES_ANSES

')

EXECUTE('
CREATE VIEW  VW_AYUDA_EXPECIONES_ANSES AS
	SELECT ID_CONVENIO AS Convenio, ID_DOMINIO AS ''Dominio'', DESCRIPCION AS ''Descripcion'',  TIPO_DOMINIO AS ''Tipo Dominio'', 
	DESC_SUB_DOMINIO AS ''Descripcion Beneficio'',TIPO_SUB_DOMINIO AS ''Tipo Sub Dominio'', SUB_DOMINIO AS ''Tipo Beneficio'' 
	FROM CONV_DOMINIOS C WITH(NOLOCK)        
	WHERE C.TZ_LOCK= 0 AND C.TIPO_DOMINIO=''E''
')
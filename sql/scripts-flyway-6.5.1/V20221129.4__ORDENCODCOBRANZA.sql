EXECUTE('
IF OBJECT_ID (''dbo.VW_ORDEN_CODIGO_CORBANZA'') IS NOT NULL
	DROP VIEW dbo.VW_ORDEN_CODIGO_CORBANZA
')

EXECUTE('
CREATE   VIEW [dbo].[VW_ORDEN_CODIGO_CORBANZA] (ORDEN,
									 CODIGOCOBRANZA,
									 CORRELATIVO,
									 TIPO,
									 CODIGOITEM)
AS 
SELECT C.ORDEN AS ''Orden'',
		C.CODIGO_COBRANZA AS ''CódigoCobranza'',
		C.CORRELATIVO AS ''Correlativo'',
		C.TIPO AS ''Tipo'',
		C.CODIGO_ITEM AS ''CodigoItem''
FROM dbo.CRE_COB_ORDEN_COBRANZA AS C WITH(NOLOCK)
WHERE C.TZ_LOCK = 0
')
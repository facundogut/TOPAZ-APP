EXECUTE('
IF OBJECT_ID (''dbo.VW_EXCLUSIONES_TIPOSGARANTIAS'') IS NOT NULL
	DROP VIEW dbo.VW_EXCLUSIONES_TIPOSGARANTIAS
')

EXECUTE('
CREATE   VIEW dbo.VW_EXCLUSIONES_TIPOSGARANTIAS AS
SELECT TG.TIPO_GARANTIA AS''Tipo Garantia'',
G.DSC_TIPO_GARANTIA AS ''Descripción'',
OP.DESCRIPCION AS ''Graduación'',
OP2.DESCRIPCION AS ''Grandes Exposiciones''
FROM CRE_EXCLUSIONES_TIPOSGARANTIAS TG WITH(NOLOCK)
INNER JOIN CRE_TIPOSGARANTIAS G ON G.TIPO_GARANTIA=TG.TIPO_GARANTIA
LEFT JOIN OPCIONES OP WITH(NOLOCK) ON OP.IDIOMA = ''E'' AND OP.NUMERODECAMPO = 44042
AND OP.OPCIONINTERNA = TG.GRADUACION
LEFT JOIN OPCIONES OP2 WITH(NOLOCK) ON OP2.IDIOMA = ''E'' AND OP2.NUMERODECAMPO = 44043
AND OP2.OPCIONINTERNA = TG.FRACCIONAMIENTO
WHERE TG.TZ_LOCK = 0
')
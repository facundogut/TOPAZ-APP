CREATE VIEW [dbo].[VW_TJD_CANAL_ORIGEN] (
							CANAL,
							DESCRIPCION_CANAL,
							ORIGEN,
							DESCRIPCION_ORIGEN
							)
AS
				SELECT CO.CANAL, 
						TC.DESCRIPCION, 
						CO.ORIGEN, 
						CO.DESCRIPCION
				FROM TJD_CANAL_ORIGEN CO WITH (nolock)
				INNER JOIN TJD_CANAL TC WITH (nolock)
					ON CO.CANAL = TC.[CODIGO] 
						AND CO.TZ_LOCK = 0 
						AND TC.TZ_LOCK = 0
GO

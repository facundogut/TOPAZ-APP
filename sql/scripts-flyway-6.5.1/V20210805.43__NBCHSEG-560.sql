s.CUENTA,
a.FECHA_OFICIO,
b.DESCRIPCION
FROM DJ_CAUSAS c INNER JOIN DJ_CAUSA_CUENTA a ON c.NRO_CAUSA=a.NRO_CAUSA INNER JOIN saldos s ON a.JTS_OID_CUENTA=s.JTS_OID 
INNER JOIN GRL_BLOQUEOS b ON s.JTS_OID=b.SALDO_JTS_OID AND b.ESTADO<>2

')
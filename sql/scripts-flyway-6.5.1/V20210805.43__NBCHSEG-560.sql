EXECUTE('
CREATE VIEW dbo.VW_DJ_CAUSAS (

[Nro. Causa],
[Juzgado],
[Año],
[Expediente],
[Tipo Causa],
[Fecha Causa],
[Estado],
[Caratula],
[Cuenta],
[Alta de la Cuenta],
[Bloqueo de la Cuenta]
)
AS

SELECT
c.NRO_CAUSA,
c.JUZGADO,
c.ANO,
c.EXPEDIENTE,
c.TIPO_CAUSA,
c.FECHA_CAUSA,
( SELECT DESCRIPCION FROM OPCIONES WHERE NUMERODECAMPO=33141 AND OPCIONINTERNA=c.ESTADO) AS ESTADO,

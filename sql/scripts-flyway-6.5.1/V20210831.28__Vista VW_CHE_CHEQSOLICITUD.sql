EXECUTE('
----
ALTER VIEW [dbo].[VW_CHE_CHEQSOLICITUD]
						(NRO_SOLICITUD,
						SUCURSAL,
						CUENTA,
						DENOMINACION_CUENTA,
						PRODUCTO,
						DESCRIPCION_PRODUCTO,
						MONEDA,
						TIPO_CHEQUERA,
						TIPO_CHEQUES,
						ESTADO,
						CANTIDAD_CHEQUES,
						TIPO_DOCUMENTO,
						NUMERO_DOCUMENTO,
						FECHA_INGRESO
						)
AS
			SELECT CH.NROSOLICITUD,
					CH.SUCURSAL,
					CH.CUENTA,
					ISNULL(V.NOMBRE_CUENTA, CH.NOMBRE_CHEQ),
					CH.PRODUCTO,
					P.C6251,
					CH.MONEDA,
					OT.DESCRIPCION,
					OU.DESCRIPCION,
					OE.DESCRIPCION,
					CH.CANTCHEQUES,
					C.TIPODOC,
					C.NUMERODOC,
					CH.FECHAINGRESO
			FROM CHE_CHEQSOLICITUD AS CH with (nolock)
				LEFT JOIN SALDOS AS S  with (nolock) ON CH.SUCURSAL=S.SUCURSAL 
														AND CH.PRODUCTO=S.PRODUCTO 
														AND CH.CUENTA=S.CUENTA 
														AND CH.MONEDA=S.MONEDA 
														AND S.OPERACION = 0 
														AND S.ORDINAL = 0 
														AND S.TZ_LOCK = 0
				LEFT JOIN VTA_SALDOS AS V with (nolock) ON S.JTS_OID = V.JTS_OID_SALDO 
											AND V.TZ_LOCK = 0
				LEFT JOIN PRODUCTOS AS P with (nolock) ON CH.PRODUCTO = P.C6250 
											AND P.TZ_LOCK = 0
				LEFT JOIN OPCIONES AS OT with (nolock) ON CH.TIPOCHEQUERA = OT.OPCIONINTERNA 
											AND OT.NUMERODECAMPO = 1072 AND OT.IDIOMA = ''E''
				LEFT JOIN OPCIONES AS OU with (nolock) ON CH.TIPOCHEQUES = OU.OPCIONINTERNA 
											AND OU.NUMERODECAMPO = 1076 
											AND OU.IDIOMA = ''E''
				LEFT JOIN OPCIONES AS OE with (nolock) ON CH.ESTADO = OE.OPCIONINTERNA 
											AND OE.NUMERODECAMPO = 1074 
											AND OE.IDIOMA = ''E''
				LEFT JOIN VW_CLI_X_DOC AS C with (nolock) ON CH.CLIENTE = C.[CODIGOCLIENTE]
			WHERE CH.TZ_LOCK = 0
----
')
CREATE VIEW dbo.VW_DJ_PERSONAS_CUENTA_BENEF (
  NUMEROPERSONA,
  TipoDocIdent, 
  NroDocIdent, 
  TipoDocFisico,
  NumDocFisico,
  NIVELAPERTURA, 
  TIPOPERSONA, 
  NOMBRE,
  Cuenta,
  NRO_CAUSA)
AS

SELECT  V.NUMEROPERSONAFJ AS NUMEROPERSONA, 
        V.TIPODOCUMENTO   AS TipoDocIdent, 
        V.NUMERODOCUMENTO AS NroDocIdent, 
        V.TIPO_DOC_FISICO AS TIPO_DOC_FISICO,
        V.NUM_DOC_FISICO  AS NUM_DOC_FISICO,
        (
          CASE U.NIVEL_APERTURA
            WHEN 0 THEN ''REGISTRO''
            WHEN 1 THEN ''B√ÅSICO''
            WHEN 3 THEN ''ACTIVOS''
          END
        ) AS NIVELAPERTURA,
        -- INICIO #001
        (V.TIPOPERSONA + '' - '' + o.DESCRIPCION) AS TIPOPERSONA,
        (
          RTRIM(
            ISNULL(U.APELLIDOPATERNO+'' '', '''') +
              ISNULL(U.APELLIDOMATERNO+'' '', '''')
          ) + '', '' + ISNULL(U.PRIMERNOMBRE, '''') + '' '' +
            ISNULL(U.SEGUNDONOMBRE, '''')
        ) AS NOMBRE,
		(SELECT CUENTA FROM SALDOS WHERE JTS_OID=B.JTS_OID_CUENTA) AS ''CUENTA'',
		B.NRO_CAUSA
FROM    DJ_BENEFICIARIOS B WITH (nolock) 
		INNER JOIN CLI_PERSONASFISICAS U (nolock) ON B.ID_BENEFICIARIO=U.NUMEROPERSONAFISICA AND
		((B.TZ_LOCK < 300000000000000 OR B.TZ_LOCK >= 400000000000000) AND (B.TZ_LOCK < 100000000000000 OR B.TZ_LOCK >= 200000000000000))
		INNER JOIN CLI_DOCUMENTOSPFPJ V (nolock) ON  U.NUMEROPERSONAFISICA = V.NUMEROPERSONAFJ
        INNER JOIN OPCIONES o (nolock)
          ON  o.NUMERODECAMPO = 1241
              AND
              o.IDIOMA = ''E''
              AND
              o.OPCIONINTERNA = V.TIPOPERSONA
WHERE   
        (
          (U.TZ_LOCK < 300000000000000 OR U.TZ_LOCK >= 400000000000000)
          AND
          (U.TZ_LOCK < 100000000000000 OR U.TZ_LOCK >= 200000000000000)
        )
        AND
        (
          (V.TZ_LOCK < 300000000000000 OR V.TZ_LOCK >= 400000000000000)
          AND
          (V.TZ_LOCK < 100000000000000 OR V.TZ_LOCK >= 200000000000000)
        )


')


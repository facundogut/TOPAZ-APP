EXECUTE('
IF OBJECT_ID (''dbo.VW_CUPO_SOBREGIRO'') IS NOT NULL
	DROP VIEW dbo.VW_CUPO_SOBREGIRO
')

EXECUTE('
CREATE   VIEW [dbo].[VW_CUPO_SOBREGIRO] (
										   MAX_CUPO,
										   SALDO_JTS_OID,
										   MONEDA,
										   SALDOACTUAL,
										   MONTO_SEGURO,
										   TASA_SEGURO)
AS 
SELECT V.MAX_CUPO, V.SALDO_JTS_OID, V.MONEDA, V.SALDOACTUAL, V.MONTO_SEGURO, V.TASA_SEGURO
FROM dbo.VTA_SEGURO_SALDO_DEUDOR V WITH(NOLOCK) 
WHERE ESTADO = ''I'' AND FECHA = (SELECT FECHAPROCESO FROM PARAMETROS WITH(NOLOCK))
')
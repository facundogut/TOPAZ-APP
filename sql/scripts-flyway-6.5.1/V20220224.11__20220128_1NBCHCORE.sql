FROM '+@ServerReference + 'BANDEJA_MOVS_IN WHERE JTS_OID_ASIENTO IN (SELECT JTS_OID FROM '+ @ServerReference+' BANDEJA_ASIENTOS_IN WHERE ORIGEN = '''+@str_EXT+'''
AND FECHAPROCESO='+ @str_fecha +')'

EXEC sp_executesql @SQLString
SET @registrosInsertados = @@RowCount

END

SET @p_msg_proceso = @p_msg_proceso +CHAR(13)+CHAR(10)+' Registros Procesados BANDEJA_MOVS_IN_EXTENDIDO: '+convert(varchar(10),@registrosInsertados)
END

EXECUTE PKG_LOG_PROCESO$proc_ins_log_proceso @p_id_proceso,@p_dt_proceso,'PKG_ACT_TABLAS_CIERRE_EXT',@p_ret_proceso,@p_msg_proceso,@c_log_tipo_informacion;

END 
END TRY

BEGIN CATCH
DECLARE
@errormessage nvarchar(4000)
SET @errormessage = ERROR_MESSAGE()
SET @p_ret_proceso = ERROR_NUMBER()
SET @p_msg_proceso= ' Se produjo un error al copiar a la tabla '+@tabla +' ['+@errormessage+']'

-- Logueo de informacion
EXECUTE PKG_LOG_PROCESO$proc_ins_log_proceso @p_id_proceso,@p_dt_proceso,'PKG_ACT_TABLAS_CIERRE_EXT',@p_ret_proceso,@p_msg_proceso,@c_log_tipo_error;

END CATCH
  
END

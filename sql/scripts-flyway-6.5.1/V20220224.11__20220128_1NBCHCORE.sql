END

IF(@registrosInsertados-@registrosBorrados)>=0
BEGIN SET @registrosProcesados = (@registrosInsertados-@registrosBorrados) 
END 
ELSE 
BEGIN
SET @registrosProcesados = 0
END


SET @p_msg_proceso = @p_msg_proceso +CHAR(13)+CHAR(10)+' Registros Procesados HISTORICO_CALIF_X_SALDO_EXTENDIDO: '+convert(varchar(10),@registrosProcesados)
END



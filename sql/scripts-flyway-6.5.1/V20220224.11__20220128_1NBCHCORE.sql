FROM '+@ServerReference+' CRE_HISTORICO_DEUDA_GARANTIAS WHERE FECHA_PROCESO='+@str_fecha 

EXEC sp_executesql @SQLString
SET @registrosInsertados = @@ROWCOUNT

END

IF(@registrosInsertados-@registrosBorrados)>=0
BEGIN SET @registrosProcesados = (@registrosInsertados-@registrosBorrados) 
END 
ELSE 
BEGIN
SET @registrosProcesados = 0
END

SET @p_msg_proceso = @p_msg_proceso +CHAR(13)+CHAR(10)+' Registros Procesados CRE_HISTORICO_DEUDA_GARANTIAS_EXTENDIDO: '+convert(varchar(10),@registrosInsertados-@registrosBorrados)

BEGIN

BEGIN 
TRUNCATE TABLE BANDEJA_ASIENTOS_IN_EXTENDIDO
END

SET @tabla = 'BANDEJA_ASIENTOS_IN_EXTENDIDO'
 
BEGIN 
SET @SQLString = N'INSERT INTO BANDEJA_ASIENTOS_IN_EXTENDIDO
SELECT JTS_OID,
ESTADO,
ORIGEN,
ASIENTO,
SUCURSAL,
FECHAPROCESO,
OPERACION,
DESCRIPCION
FROM ' +@ServerReference+' BANDEJA_ASIENTOS_IN WHERE ORIGEN='''+@str_EXT+''' AND FECHAPROCESO='+@str_fecha 

EXEC sp_executesql @SQLString
SET @registrosInsertados = @@RowCount

END

SET @p_msg_proceso = @p_msg_proceso +CHAR(13)+CHAR(10)+' Registros Procesados BANDEJA_ASIENTOS_IN_EXTENDIDO: '+convert(varchar(10),@registrosInsertados)
END

BEGIN

SET @tabla = 'BANDEJA_MOVS_IN_EXTENDIDO'

BEGIN 
TRUNCATE TABLE BANDEJA_MOVS_IN_EXTENDIDO
END

BEGIN 

SET @SQLString = N' INSERT INTO BANDEJA_MOVS_IN_EXTENDIDO
SELECT
JTS_OID,
JTS_OID_ASIENTO,
SUCURSAL,
PRODUCTO,
CUENTA,
MONEDA,
OPERACION,

CONTRA_MONEDA,
RUBRO,
SUCURSAL_CUENTA,
GANANCIA_PERDIDA,
RESULTADO,
RUBRO_RESULTADO FROM '+@ServerReference +'GRL_OPE_CAMBIO WHERE FECHA_PROCESO='+ @str_fecha +' AND ASIENTO IN
(SELECT ASIENTO FROM '+ @ServerReference+' BANDEJA_MOVS_IN WHERE JTS_OID IN (SELECT JTS_OID FROM '+ @ServerReference+' BANDEJA_ASIENTOS_IN WHERE ORIGEN = '''+@str_EXT+''') AND SUCURSAL IN ((SELECT SUCURSAL FROM '+ @ServerReference+' BANDEJA_MOVS_IN WHERE JTS_OID_ASIENTO IN (SELECT JTS_OID FROM '+ @ServerReference+' BANDEJA_ASIENTOS_IN WHERE ORIGEN='''+@str_EXT+'''))))'

EXEC sp_executesql @SQLString
SET @registrosInsertados = @@ROWCOUNT

END

IF(@registrosInsertados-@registrosBorrados)>=0
BEGIN SET @registrosProcesados = (@registrosInsertados-@registrosBorrados) 
END 
ELSE 
BEGIN
SET @registrosProcesados = 0
END

SET @p_msg_proceso = @p_msg_proceso +CHAR(13)+CHAR(10)+' Registros Procesados GRL_OPE_CAMBIO_EXTENDIDO : '+convert(varchar(10),@registrosProcesados)
END


BEGIN

SET @tabla = 'HISTORICO_DEVENGAMIENTO_EXTENDIDO'

BEGIN 

DELETE FROM HISTORICO_DEVENGAMIENTO_EXTENDIDO
WHERE FECHA = @fechaProceso;

SET @registrosBorrados = @@RowCount 

END

BEGIN

SET @SQLString = N' INSERT INTO HISTORICO_DEVENGAMIENTO_EXTENDIDO
SELECT TZ_LOCK,
FECHA,
SALDO_JTS_OID,
NRO_CUOTA,
ESTADO_ATRASO,

MORA_DEVENG_VIGENTE_CONT,
MORA_DEVENG_VENCIDO_CONT,
MORA_DEVENG_SUSPENSO_CONT,
MORA_TASA_INT_DEVENGADA,
MORA_TASA_INT_DEVENG_VIGENTE,
MORA_TASA_INT_DEVENG_VIGE_CONT,
MORA_TASA_INT_DEVENG_VENC_CONT,
MORA_TASA_INT_DEVENG_SUSP_CONT,
MORA_DEVENGADA_ULT_PROC,
MORA_TASA_INT_DEVENG_ULT_PROC,
ICMORA_DEVENGADO,

SET @tabla = 'CRE_HISTORICO_DEUDA_GARANTIAS_EXTENDIDO'

BEGIN
DELETE FROM CRE_HISTORICO_DEUDA_GARANTIAS_EXTENDIDO
WHERE FECHA_PROCESO= @fechaProceso 
SET @registrosBorrados = @@RowCount  

END 

BEGIN 
SET @SQLString = N' INSERT INTO CRE_HISTORICO_DEUDA_GARANTIAS_EXTENDIDO
SELECT 
SALDO_JTS_OID,
TIPO_ASISTENCIA,
FECHA_PROCESO,
NRO_GARANTIA,

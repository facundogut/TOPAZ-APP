 ALTER PROCEDURE PKG_ACT_TABLAS_CIERRE_EXT (
 @p_id_proceso NUMERIC,
 @p_dt_proceso  DATETIME,
 @fechaProceso  DATE,
 @p_ret_proceso NUMERIC OUTPUT,
 @p_msg_proceso VARCHAR (max) OUTPUT
)

AS

 DECLARE
 @tabla                     varchar(max),
 @registrosBorrados         NUMERIC(10,0),
 @registrosInsertados       NUMERIC(10,0),
 @c_log_tipo_error			varchar(1),
 @c_log_tipo_informacion    varchar(1),
 @ServerReference 			nvarchar(128),
 @SQLString					nvarchar(3500),
 @str_fecha                 nvarchar(12),
 @registrosProcesados       NUMERIC(10,0),
 @str_EXT					varchar(12)

 BEGIN
 
 BEGIN TRY 
 
 SET @p_ret_proceso = 1;

SET @c_log_tipo_error      = 'E';

SET @c_log_tipo_informacion = 'I';

SET @ServerReference = 'dbo.';

SET DATEFORMAT YMD;

--YYYYMMDD formato: 112
 SET @str_fecha =  ''''+CONVERT(CHAR(8), @fechaProceso, 112)+'''';

SET @str_EXT = 'EXT';

BEGIN

SET @tabla = 'HISTORICOTIPOSCAMBIO_EXTENDIDO'
 
BEGIN

DELETE FROM HISTORICOTIPOSCAMBIO_EXTENDIDO
WHERE FECHA_COTIZACION = @fechaProceso;

SET @registrosBorrados = @@RowCount

END

BEGIN

SET @SQLString = N'INSERT INTO HISTORICOTIPOSCAMBIO_EXTENDIDO
SELECT TZ_LOCK,
CODIGO_TIPO_CAMBIO,
MONEDA,
FECHA_COTIZACION,
TIPO_CAMBIO_COMPRA,
TIPO_CAMBIO_VENTA,
ARBITRAJE_COMPRA,
ARBITRAJE_VENTA,
TIPO_CAMBIO_OFICIAL,
ARBITRAJE_CIERRE_OFICIAL
FROM '+ @ServerReference+ 'HISTORICOTIPOSCAMBIO WHERE FECHA_COTIZACION = '+ @str_fecha

EXEC sp_executesql @SQLString
SET @registrosInsertados = @@ROWCOUNT
END

IF(@registrosInsertados-@registrosBorrados)>=0
BEGIN SET @registrosProcesados = (@registrosInsertados-@registrosBorrados) END

ELSE
BEGIN
SET @registrosProcesados = 0
END


SET @p_msg_proceso = ' Registros Procesados HISTORICOTIPOSCAMBIO_EXTENDIDO : '+convert(varchar(10),@registrosProcesados)

END

BEGIN

SET @tabla = 'GRL_OPE_CAMBIO_EXTENDIDO'

BEGIN

SET @SQLString = N'DELETE FROM  GRL_OPE_CAMBIO_EXTENDIDO
WHERE FECHA_PROCESO= '+@str_fecha +' AND ASIENTO IN
(SELECT ASIENTO FROM '+ @ServerReference + 'BANDEJA_MOVS_IN WHERE JTS_OID IN (SELECT JTS_OID FROM '+ @ServerReference + ' BANDEJA_ASIENTOS_IN WHERE ORIGEN='''+@str_EXT+''')
 AND SUCURSAL IN ((SELECT SUCURSAL FROM '+ @ServerReference + 'BANDEJA_MOVS_IN WHERE JTS_OID_ASIENTO IN (SELECT JTS_OID FROM '+ @ServerReference + 'BANDEJA_ASIENTOS_IN WHERE ORIGEN='''+@str_EXT+''' ))))'

EXEC sp_executesql @SQLString
SET @registrosBorrados = @@RowCount

END

BEGIN

SET @SQLString = N' INSERT INTO GRL_OPE_CAMBIO_EXTENDIDO
SELECT TZ_LOCK,
JTS_OID,
ASIENTO,
FECHA_PROCESO,
SUCURSAL,
CODIGO_CLIENTE,
TIPO_CAMBIO,
TIPO_CAMBIO_PIZARRA,
EXPOSICION,
IMPORTE_ME,

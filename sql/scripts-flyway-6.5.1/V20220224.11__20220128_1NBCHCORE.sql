ICMORA_CONTABILIZADO,
INTERES_A_LIQUIDAR,
ICMORA_DEVEN_VIGENTE,
ICMORA_DEVEN_NP_VIG,
ICMORA_DEVEN_VIGENTE_CONT,
ICMORA_DEVEN_SUSPENSO_CONT,
ICMORA_DEVEN_VENCIDO_CONT,
ICMORA_PERCIBIDA,
INTERES_PRIMARIO_DEVENG_CONT,
INTERES_SECUNDARIO_DEVENG_CONT
FROM '+ @ServerReference +' HISTORICO_DEVENGAMIENTO WHERE FECHA='+@str_fecha 

EXEC sp_executesql @SQLString
SET @registrosInsertados = @@ROWCOUNT

END

IF(@registrosInsertados-@registrosBorrados)>=0
BEGIN SET @registrosProcesados = (@registrosInsertados-@registrosBorrados) 
END 
ELSE 
BEGIN
SET @registrosProcesados = 0
END

SET @p_msg_proceso = @p_msg_proceso +CHAR(13)+CHAR(10)+' Registros Procesados HISTORICO_DEVENGAMIENTO_EXTENDIDO : '+convert(varchar(10),@registrosProcesados)
END



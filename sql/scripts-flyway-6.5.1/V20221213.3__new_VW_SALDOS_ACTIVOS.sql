EXECUTE ('
CREATE OR ALTER VIEW [dbo].[VW_SALDOS_ACTIVOS] (
		      						    	[CODIGOCLIENTE],
											[ESTADO],
											[CODIGOBLOQUEO],
										    [COD_CANCELACION],
										    [JTS_OID]
											)
AS 

SELECT 
	c.CODIGOCLIENTE, 
	c.ESTADO, 
	c.CODIGOBLOQUEO, 
	s.C1651, 
	s.JTS_OID
FROM CLI_CLIENTES c
INNER JOIN SALDOS s ON s.C1803=c.CODIGOCLIENTE
WHERE 
		s.C1651<>''1'' AND
		((c.TZ_LOCK < 300000000000000 OR c.TZ_LOCK >= 400000000000000) AND 
		(c.TZ_LOCK < 100000000000000 OR c.TZ_LOCK >= 200000000000000))
')
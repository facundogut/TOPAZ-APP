EXECUTE('
-------------------------------
--NUEVA VISTA PODER X ENTIDAD--
-------------------------------
CREATE VIEW VW_PYF_PODER_X_ENTIDAD
(TIPO_PODER,DESCRIPCION_TIPO_PODER,TIPO_ENTIDAD,DESCRIPCION_TIPO_ENTIDAD)
AS
SELECT X.TIPO_PODER, P.DESCRIPCION, X.TIPO_ENTIDAD, E.DESCRIPCION 
FROM PYF_TIPOPODER_X_TIPOENTIDAD AS X
LEFT JOIN PYF_TIPOENTIDAD AS E ON X.TIPO_ENTIDAD = E.TIPO_ENTIDAD AND E.TZ_LOCK = 0
LEFT JOIN PYF_TIPOPODERES AS P ON X.TIPO_PODER = P.TIPO_PODER AND P.TZ_LOCK = 0
WHERE X.TZ_LOCK = 0
----
')